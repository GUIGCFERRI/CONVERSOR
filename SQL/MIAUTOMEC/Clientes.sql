SELECT
    CLIENTE.CDCLIENTE AS CODIGO,
    CLIENTE.RAZAO AS NOME,
    CLIENTE.CLIENTE AS FANTASIA,
    CLIENTE.CGC AS CNPJ_CNPF,
    CLIENTE.INSCEST AS IE_RG,
    CLIENTE.INSCESTMUNIC AS IM,
    CLIENTE.ENDERECO AS ENDERECO,
    CLIENTE.BAIRRO AS BAIRRO,
    CLIENTE.NUMERO AS NUMERO,
    MUNICIPIO.MUNICIPIO AS CIDADE,
    CLIENTE.UF AS UF,
    replace(CLIENTE.CEP,'-','') AS CEP,
    CLIENTE.TELEFONE AS TELEFONE,
    CLIENTE.FAX AS FAX,
    CLIENTE.CELULAR AS CELULAR,
    CLIENTE.EMAIL AS EMAIL,
    CLIENTE.OBS AS OBSERVACOES,
    CLIENTE.NASCIMENTO AS NASCIMENTO,
    CLIENTE.MAE AS MAE,
    CLIENTE.PAI AS PAI,
    CAST (CLIENTE.DTULTCOMPRA AS DATE) AS ULTIMA_COMPRA,
    CAST (CLIENTE.CADASTRO AS DATE) AS CADASTRO
FROM CLIENTE
LEFT JOIN MUNICIPIO ON MUNICIPIO.CDMUNICIPIO = CLIENTE.CDMUNICIPIO
