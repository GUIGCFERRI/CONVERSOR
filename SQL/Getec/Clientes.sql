SELECT
	PAR_PKN_CODIGO AS CODIGO,
	PAR_A_RAZAOSOCIAL AS NOME,
	PAR_A_NOME_FANTASIA AS FANTASIA,
	CONCAT (COALESCE(PAR_A_LOGRADOURO, ''),' ',COALESCE(PAR_A_ENDERECO, '')) AS ENDERECO,
	PAR_A_NUMERO AS NUMERO,
	PAR_A_COMPLEMENTO AS COMPLEMENTO,
	PAR_A_BAIRRO AS BAIRRO,
	PAR_A_CIDADE AS CIDADE,
	PAR_A_ESTADO AS UF,
	PAR_A_CEP AS CEP,
	CASE WHEN PAR_A_CPF = '' OR PAR_A_CPF IS NULL THEN PAR_A_CNPJ_CPF ELSE PAR_A_CPF END AS CNPJ_CNPF,
	CASE WHEN PAR_A_RG = '' OR PAR_A_RG IS NULL THEN PAR_A_CGF ELSE PAR_A_RG END AS IE_RG,
	PAR_INSC_MUNICIPAL AS IM,
	CASE PAR_A_SEXO WHEN 'Masculino' THEN 'M' WHEN 'F' THEN 'Feminino' END AS SEXO,
	PAR_A_FONE1 AS TELEFONE,
	PAR_A_FONE2 AS CELULAR,
	PAR_A_FONE3 AS FAX,
	PAR_A_FONE4 AS PERSONAL2,
	PAR_A_CONTATO AS CONTATO,
	PAR_A_EMAIL AS EMAIL,
	PAR_N_VALOR_CREDITO AS LIMITE_CREDITO,
	CAST(PAR_D_DATANASC AS DATE) AS NASCIMENTO,	
	CAST(COALESCE(PAR_D_DATA_CADASTRO, GETDATE()) AS DATE) AS CADASTRO,
	CAST(PAR_D_DATA_ULTIMA_COMPRA AS DATE) AS ULTIMA_VENDA,
	PAR_A_OBS_FINANCEIRA AS OBSERVACOES,
	CONCAT (COALESCE(PAR_A_LOGRADOURO_COB, ''),' ',COALESCE(PAR_A_ENDERECO_COB, '')) AS COB_ENDERECO,
	PAR_A_NUMERO_COB AS COB_NUMERO,
	PAR_A_COMPLEMENTO_COB AS COB_COMPLEMENTO,
	PAR_A_BAIRRO_COB AS COB_BAIRRO,
	PAR_A_CIDADE_COB AS COB_CIDADE,
	PAR_A_ESTADO_COB AS COB_UF,
	PAR_A_CEP_COB AS COB_CEP,
	PAR_A_ESTADOCIVIL AS EST_CIVIL
FROM
	TB_PAR_PARTICIPANTE
WHERE PAR_A_TIPO = 'CLIENTE'