SELECT
    CLIENTES.RAZAOSOCIAL AS NOME,
    CLIENTES.NOMEFANTASIA AS FANTASIA,
    CLIENTES.CONTATO AS CONTATO,
    CLIENTES.CGC AS CNPJ_CNPF,
    CLIENTES.INSCESTADUAL AS IE_RG,
    CLIENTES.IM AS IM,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.UF AS UF,
    CLIENTES.CEP AS CEP,
    CLIENTES.DDD  || '' || CLIENTES.FONE AS TELEFONE,
    CLIENTES.DDD  || '' || CLIENTES.FAX AS FAX,
    CLIENTES.DDD  || '' || CLIENTES.CELULAR AS CELULAR,
    CLIENTES.EMAIL AS EMAIL, 
    CLIENTES.MEMO AS OBSERVACOES,
    CLIENTES.LIMITECREDITO AS LIMITE_CREDITO,
    CLIENTES.NOMEPAI AS PAI,
    CLIENTES.NOMEMAE AS MAE, 
    CLIENTES.SEXO AS SEXO,
    CLIENTES.NOMECONJUGE AS CONJUGE,
    CAST (CLIENTES.DATACADASTRO AS DATE) AS CADASTRO,
    CAST (CLIENTES.ANIVERSARIO AS DATE) AS NASCIMENTO,
    CAST (CLIENTES.ULTIMACOMPRA AS DATE) AS ULTIMA_VENDA 
FROM CLIENTES
