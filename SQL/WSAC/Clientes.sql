SELECT
    CLI_CODIGO AS CODIGO,
    CLI_NOME AS NOME,
    CLI_FANTASIA AS FANTASIA,
    CLI_CONTATO AS CONTATO,
    case when CLI_CPF = '' or CLI_CPF is null then CLI_CGC_CPF else CLI_CPF END AS CNPJ_CNPF,
    case when CLI_IE = '' or CLI_IE is null then CLI_RG_INSCRICAO else CLI_IE END AS IE_RG,
    CLI_ENDERECO AS ENDERECO,
    CLI_COMPLENTO AS COMPLEMENTO,
    CLI_BAIRRO AS BAIRRO,
    CLI_CIDADE AS CIDADE,
    CLI_ESTADO AS UF,
    CLI_CEP AS CEP,
    CLI_DDD_FONE  ||''|| CLI_FONE AS TELEFONE,
    CLI_DDD_FONE2  ||''|| CLI_FONE2 AS FAX,        
    CLI_DDD_CELULAR  ||''|| CLI_CELULAR AS CELULAR,
    CLI_EMAIL AS EMAIL,
    CLI_OBSERVACAO AS OBSERVACOES,
    CLI_PROFISSAO AS PROFISSAO,
    CLI_PAI AS PAI,
    CLI_MAE AS MAE,
    CLI_SEXO AS SEXO,
    CCJ_NOME AS CONJUGE,
    CLI_NASCIMENTO AS NASCIMENTO,
    CLI_ESTADO_CIVIL AS EST_CIVIL,    
    CLI_DATACADASTRO  AS CADASTRO,
    case when CLI_ATIVO = 'A' then 'Ativo' else 'Inativo' end as SITUACAO
FROM CLIENTE