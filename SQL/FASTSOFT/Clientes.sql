SELECT
    CLIENTE.CODIGO_CLIENTE AS CODIGO,
    CLIENTE.NOME AS NOME,
    CLIENTE.FANTASIA AS FANTASIA,
    CLIENTE.CONTATO AS CONTATO,
    CLIENTE.CNPJ_CPF AS CNPJ_CNPF,
    case when CLIENTE.RG = '' or CLIENTE.RG is null then CLIENTE.IE else CLIENTE.RG END AS IE_RG,
    CLIENTE.IM AS IM,
    CLIENTE.ENDERECO AS ENDERECO,
    CLIENTE.COMPLEMENTO AS COMPLEMENTO,
    CLIENTE.NUMERO AS NUMERO,
    CLIENTE.BAIRRO AS BAIRRO,
    CIDADE.NOME AS CIDADE,
    CIDADE.UF AS UF,
    CLIENTE.CEP AS CEP,
    CLIENTE.TELEFONE1 AS TELEFONE,
    CLIENTE.TELEFONE2 AS CELULAR,
    CLIENTE.TELEFONE3 AS FAX,
    CLIENTE.EMAIL AS EMAIL,
    CLIENTE.SEXO AS SEXO,
    CLIENTE.OBSERVACAO AS OBSERVACOES,
    CLIENTE.MAE AS MAE,
    CLIENTE.PAI AS PAI,
    CLIENTE.NOME_CONJUGUE AS CONJUGE,
    CAST (CLIENTE.DATA_NASCIMENTO AS DATE) AS NASCIMENTO,
    CAST (CLIENTE.LIMITE_CREDITO AS DATE) AS LIMITE_CREDITO,
    CAST (CLIENTE.DATA_CAD AS DATE) AS CADASTRO
FROM CLIENTE
LEFT JOIN CIDADE ON CIDADE.CODIGO_CIDADE = CLIENTE.CODIGO_CIDADE