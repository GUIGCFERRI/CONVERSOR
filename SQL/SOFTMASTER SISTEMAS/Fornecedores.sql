SELECT
    FORNECEDOR.ID AS CODIGO,
    FORNECEDOR.RAZAOSOCIAL AS NOME,
    FORNECEDOR.FANTASIA AS FANTASIA,
    FORNECEDOR.CONTATO AS CONTATO,
    FORNECEDOR.CNPJ AS CNPJ_CNPF,
    case when FORNECEDOR.INSCRICAO = '' or FORNECEDOR.INSCRICAO is null then FORNECEDOR.RG else FORNECEDOR.INSCRICAO END AS IE_RG,
    FORNECEDOR.INSCRICAO,
    FORNECEDOR.RG,
    FORNECEDOR.TIPORUA ||' '|| FORNECEDOR.NOMERUA AS ENDERECO,
    FORNECEDOR.COMPLEMENTO_END AS COMPLEMENTO,
    FORNECEDOR.NUMERO AS NUMERO,
    FORNECEDOR.BAIRRO AS BAIRRO,
    FORNECEDOR.CIDADE AS CIDADE,
    FORNECEDOR.ESTADO AS UF,
    FORNECEDOR.CEP AS CEP,
    FORNECEDOR.DDD ||' ' || FORNECEDOR.FONE AS TELEFONE,
    FORNECEDOR.DDD ||' ' || FORNECEDOR.FAX AS FAX,
    FORNECEDOR.EMAIL AS EMAIL,
    FORNECEDOR.OBSERVACAO AS OBSERVACOES 
FROM FORNECEDOR