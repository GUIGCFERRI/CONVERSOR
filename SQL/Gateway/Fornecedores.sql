SELECT
    FORNECEDORES.CODIGO AS CODIGO,
    FORNECEDORES.NOME AS NOME,
    FORNECEDORES.FANTASIA AS FANTASIA,
    FORNECEDORES.CONTATO AS CONTATO,
    case when FORNECEDORES.CNPJ = '' or FORNECEDORES.CNPJ is null then FORNECEDORES.CPF else FORNECEDORES.CNPJ END AS CNPJ_CNPF,
    case when FORNECEDORES.IE = '' or FORNECEDORES.IE is null then FORNECEDORES.RG else FORNECEDORES.IE END AS IE_RG,
    FORNECEDORES.ENDERECO AS ENDERECO,
    FORNECEDORES.NUMERO AS NUMERO,
    FORNECEDORES.COMPLEMENTO AS COMPLEMENTO,
    FORNECEDORES.BAIRRO AS BAIRRO,
    FORNECEDORES.CEP AS CEP,
    FORNECEDORES.UF AS UF,
    FORNECEDORES.CIDADE AS CIDADE,
    FORNECEDORES.TELEFONE AS TELEFONE,
    FORNECEDORES.CELULAR AS CELULAR,
    FORNECEDORES.FAX AS CELULAR,
    FORNECEDORES.EMAIL AS EMAIL,
    FORNECEDORES.OBSERVACOES AS OBSERVACOES
FROM FORNECEDORES
