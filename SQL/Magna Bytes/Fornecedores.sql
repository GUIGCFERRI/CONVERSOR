SELECT
	CODIGO,
	RAZAO_SOCIAL AS NOME,
	FANTASIA,
	CNPJ AS CNPJ_CNPF,
	CASE
		WHEN RG = ''
		OR RG IS NULL THEN INSCRICAO
		ELSE RG
	END AS IE_RG,
	CEP,
	CIDADE,
	UF,
	ENDERECO,
	COMPLEMENTO,
	NUMERO,
	SETOR AS BAIRRO,
	TELEFONE,
	FAX,
	CONTATO,
	CELULAR,
	EMAIL,
	OBS AS OBSERVACOES
FROM PESSOAS
WHERE CODIGO <> '0'
AND FORNECEDOR = '1';
