SELECT
	P.CODIGO,
	P.COD_BARRAS AS BARRAS,
	P.GRUPO,
	P.SUBGRUPO AS FAMILIA,
	P.DESCRICAO,
	P.REF_FABRICA AS COD_FABRICANTE,
	P.REF_ORIGINAL AS PERSONAL1,
	P.PRE_VENDA AS PRECO_VENDA,
	P.ESTOQUE AS QTD,
	P.UM AS UND,
	P.DATA_EST_INICIAL AS DATA_CADASTRO,
	P.EST_MINIMO AS QTD_IDEAL,
	P.CUSTO_REAL AS PRECO_CUSTO,
	P.CUSTO_MEDIO,
	P.LUCRO AS MARGEM_LUCRO,	
	P.PRATELEIRA AS PERSONAL2,
	P.ULTIMA_COMPRA AS ULTIMA_COMPRA,
	P.ULTIMA_VENDA AS ULTIMA_VENDA,
	P.COMISSAO,
	P.NCM AS COD_NCM,
	P.IPI AS ALIQ_IPI,
	P.CST_ICMS AS ST,
	P.CST_PIS_S AS PIS_CODIGO,
	P.CST_PIS_E AS PISE_CODIGO,
	P.ALIQUOTA_PIS AS PIS_ALIQ_NOR,
	P.CST_COFINS_S AS COFINS_CODIGO,
	P.CST_COFINS_E AS COFINSE_CODIGO,
	P.ALIQUOTA_COFINS AS COFINS_ALIQ_NOR,
	P.CFOP AS CF,
	LEFT (P.CFOP,3) AS ELO,
	LEFT (P.ORIGEM,1) AS OST,
	T.DESCRICAO AS CARACTERISTICAS,
	F.NOME AS FORNECEDOR
FROM
	PRODUTOS P
	LEFT JOIN TIPOPRODUTOS T ON T.CODIGO = P.TIPO_PRODUTO  
	LEFT JOIN FORNECEDORES F ON F.CODIGO = P.FORNECEDOR
