SELECT
    CLIENTES.CODIGO_CLIENTE AS CODIGO,
    CLIENTES.NOME_CLIENTE AS NOME,
    CLIENTES.RAZAO_SOCIAL AS FANTASIA,
    CLIENTES.SEXO AS SEXO,
    CLIENTES.DATA_NASCIMENTO AS NASCIMENTO,
    CLIENTES.CPF_CNPJ AS CNPJ_CNPF,
    CLIENTES.RG_INSC AS IE_RG,
    CLIENTES.INSC_MUNICIPAL AS IM,
    CLIENTES.NATURALIDADE AS NATURALIDADE,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.NUMERO AS NUMERO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CEP AS CEP,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.UF AS UF,
    CLIENTES.PONTO_REFERENCIA AS COMPLEMENTO,
    CLIENTES.TELEFONE AS TELEFONE,
    CLIENTES.TELEFONE_CELULAR AS CELULAR,
    CLIENTES.FAX AS FAX,
    CLIENTES.MAIL AS EMAIL,
    CLIENTES.CONJUGE AS CONJUGE,
    CLIENTES.FILIACAO_PAI AS PAI,
    CLIENTES.FILIACAO_MAE AS MAE,
    CLIENTES.DATA_CADASTRO AS CADASTRO,
    CLIENTES.OBSERVACOES AS OBSERVACOES,
    CLIENTES.ESTADO_CIVIL AS EST_CIVIL
FROM CLIENTES
WHERE CLIENTES.CODIGO_CLIENTE <> '0'
