SELECT
	TBPRODUTO.CODIGO AS CODIGO,
	TBPRODUTO.CBARRA AS BARRAS,
	TBPRODUTO.DESCRICAO AS DESCRICAO,
	TBPRODUTO.UN AS UND,
	TBPRODUTO.QTD AS QTD,
	TBPRODUTO.EMINIMO AS QTD_IDEAL,
	TBPRODUTO.PESO AS PESO,
	TBPRODUTO.VRCUSTO AS PRECO_CUSTO,
	TBPRODUTO.PORCENTAGEM AS MARGEM_LUCRO,
	TBPRODUTO.VRVENDA AS PRECO_VENDA,
	TBPRODUTO.PRECO_ATACADO AS PRECO_ATACADO,
	TBPRODUTO.SETOR AS GRUPO,
	TBPRODUTO.MARCA AS CARACTERISTICAS,
	TBPRODUTO.REFERENCIA AS COD_FABRICANTE,
	TBPRODUTO.CEST AS PERSONAL6,
	TBPRODUTO.CST_ICMS AS ST,
	TBPRODUTO.NCM AS COD_NCM,
	TBPRODUTO.DATA_ESTOQUE AS DATA_CADASTRO,
	TBPRODUTO.PESO,
	CASE
		TBPRODUTO.ATIVO WHEN 'S' THEN 'Ativo'
		ELSE 'Inativo'
	END AS SITUACAO,
	TBPRODUTO.QTD_ATACADO AS QTD_PRECO_ATACADO,
	TBPRODUTO.COMISSAO,
	TBPRODUTO.FABRICANTE AS FAMILIA,
	TBPRODUTO.FORNECEDOR,
	TBPRODUTO.REFERENCIA AS PERSONAL1,
	TBPRODUTO.CODIGO_ANP AS COD_ANP,
	LEFT (TBPRODUTO.CFOP,3) AS ELO,
	TBPRODUTO.ORIG AS OST,
	TBPRODUTO.CST_IPI AS IPI_CODIGO_VENDA,
	TBPRODUTO.CST_PIS AS PIS_CODIGO,
	TBPRODUTO.CST_CONFINS AS COFINS_CODIGO,
	TBPRODUTO.IPI_TAXA AS ALIQ_IPI_VENDA,
	TBPRODUTO.PIS_TAXA AS PIS_ALIQ_NOR,
	TBPRODUTO.COFINS_TAXA AS COFINS_ALIQ_NOR
FROM
	TBPRODUTO
