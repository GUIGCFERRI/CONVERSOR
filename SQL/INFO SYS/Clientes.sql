SELECT
    CLIENTES.CODIGO AS CODIGO,
    CLIENTES.RAZAO AS NOME,
    CLIENTES.NOME AS FANTASIA,
    CLIENTES.CNPJ_CPF AS CNPJ_CNPF,
    case when CLIENTES.INSCRICAO_ESTADUAL = '' or CLIENTES.INSCRICAO_ESTADUAL is null then CLIENTES.INSCRICAO_RG else CLIENTES.INSCRICAO_ESTADUAL END AS IE_RG,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.NUMERO AS NUMERO,
    CLIENTES.COMPLEMENTO AS COMPLEMENTO,
    BAIRROS.NOME AS BAIRRO,
    CIDADES.NOME AS CIDADE,
    CLIENTES.CEP AS CEP,
    CLIENTES.UF AS UF,
    replace(replace(CLIENTES.FONE,'/',''),'-','') AS TELEFONE,
    CLIENTES.FAX AS FAX,    
    CLIENTES.CELULAR AS CELULAR,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.ESTADO_CIVIL AS EST_CIVIL,
    CLIENTES.CONJUGE AS CONJUGE,
    CLIENTES.PAI AS PAI,
    CLIENTES.MAE AS MAE,
    CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO,
    CLIENTES.SEXO AS SEXO,
    CLIENTES.NATURALIDADE AS NATURALIDADE,
    CLIENTES.INFORMACOES AS OBSERVACOES,
    CLIENTES.CADASTRO AS CADASTRO,
    CLIENTES.NASCIMENTO AS NASCIMENTO,
    CLIENTES.DATA_ULT_COMPRA AS ULTIMA_COMPRA,
    case when CLIENTES.ATIVO = 'S' then 'Ativo' else 'Inativo' end as SITUACAO
FROM CLIENTES
LEFT JOIN CIDADES ON CIDADES.CODIGO = CLIENTES.CIDADE
LEFT JOIN BAIRROS ON BAIRROS.CODIGO = CLIENTES.BAIRRO