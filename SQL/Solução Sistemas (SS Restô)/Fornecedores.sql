FORNECEDORES.CELULARES AS CELULAR,
FORNECEDORES.FAX,
FORNECEDORES.EMAIL,
FORNECEDORES.CNPJ AS CNPJ_CNPF,
FORNECEDORES.IE AS IE_RG,
FORNECEDORES.CONTATO,
FORNECEDORES.[OBSERVAÇÕES] AS OBSERVACOES 
