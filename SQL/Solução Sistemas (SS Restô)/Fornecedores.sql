SELECT 
FORNECEDORES.[CÓDIGO] AS CODIGO,
FORNECEDORES.NOME,
FORNECEDORES.APELIDO AS FANTASIA,
FORNECEDORES.CEP,
FORNECEDORES.[ENDEREÇO] AS ENDERECO,
FORNECEDORES.[NÚMERO] AS NUMERO,
FORNECEDORES.COMPLEMENTO,
FORNECEDORES.BAIRRO,
FORNECEDORES.CIDADE,
FORNECEDORES.UF,
