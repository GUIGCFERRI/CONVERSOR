SELECT
  CLIE.CODI AS CODIGO,
  CLIE.NOME AS NOME,
  CLIE.ENDE AS ENDERECO,
  CLIE.BAIR AS BAIRRO,
  CLIE.CIDA AS CIDADE,
  CLIE.ESTA AS ESTADO,
  CLIE.CEPC AS CEP,
  CLIE.DTNA AS NASCIMENTO,
  case when CLIE.NCPF = '' or CLIE.NCPF is null then CLIE.CGCC else CLIE.NCPF END as CNPJ_CNPF,
  case when CLIE.DOID = '' or CLIE.DOID is null then CLIE.INSC else CLIE.DOID END as IE_RG,
  CLIE.FON1 AS TELEFONE,
  CLIE.FON2 AS CELULAR,
  CLIE.NPAI AS PAI,
  CLIE.NMAE AS MAE,
  CLIE.DTCA AS CADASTRO,
  CLIE.FAXC AS FAX,
  CLIE.SEXO AS SEXO,
  CLIE.EMAI AS EMAIL
FROM CLIE
