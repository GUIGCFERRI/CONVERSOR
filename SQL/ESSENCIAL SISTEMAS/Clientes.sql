SELECT
    CLIENTE.CODIGO AS CODIGO,
    CLIENTE.NOME AS NOME,
    CLIENTE.RAZAO AS FANTASIA,
    replace (replace(CLIENTE.CNPJ,'.','') ,'-', '') AS CNPJ_CNPF,
    CLIENTE.INSCRICAO AS IE_RG,
    CLIENTE.ENDERECO AS ENDERECO,
    CLIENTE.BAIRRO AS BAIRRO,
    CLIENTE.NUMERO AS NUMERO,
    CLIENTE.COMPLEMENTO AS COMPLEMENTO,
    CIDADE.NOME AS CIDADE,
    CIDADE.ESTADO AS UF,
    CLIENTE.CEP AS CEP,
    CLIENTE.FONE1 AS TELEFONE,
    CLIENTE.CELULAR AS CELULAR,
    CLIENTE.FAX AS FAX,
    CLIENTE.EMAIL AS EMAIL,
    CLIENTE.NOMEPAI AS PAI,
    CLIENTE.NOMEMAE AS MAE,
    CLIENTE.ESTADOCIVIL AS EST_CIVIL,
    CLIENTE.SEXO AS SEXO,
    CLIENTE.LIMITECREDITO AS LIMITE_CREDITO,
    CLIENTE.OBSERVACOES AS OBSERVACOES,
    CLIENTE.ULTIMACOMPRA AS ULTIMA_VENDA,
    CLIENTE.DATACADASTRO AS CADASTRO
FROM CLIENTE
LEFT JOIN cidade ON CIDADE.codigo = CLIENTE.CIDADE