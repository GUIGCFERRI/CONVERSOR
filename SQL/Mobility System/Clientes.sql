SELECT
    CLIENTES.ID AS CODIGO,
    CLIENTES.NOME AS NOME,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.COMPLEMENTO AS COMPLEMENTO,
    CLIENTES.BAIRRO AS BAIRRO, 
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.UF AS UF,
    CLIENTES.CEP AS CEP,
    case when CLIENTES.CPF = '' or CLIENTES.CPF is null then CLIENTES.CNPJ else CLIENTES.CPF END AS CNPJ_CNPF,
    case when CLIENTES.RG = '' or CLIENTES.RG is null then CLIENTES.INSC_ESTADUAL else CLIENTES.RG END AS IE_RG,
    CLIENTES.DDD ||''|| CLIENTES.TELEFONE AS TELEFONE,
    CLIENTES.DDD_CEL ||''|| CLIENTES.CELULAR AS CELULAR,
    CLIENTES.DATA_CADASTRO AS CADASTRO,
    CLIENTES.DATA_ANIVERSARIO AS NASCIMENTO,
    CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO,
    CLIENTES.DEBITO AS VALOR_EM_ATRASO,
    CLIENTES.COMENTARIOS AS OBSERVAOES,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.END_NUM AS NUMERO
FROM CLIENTES
