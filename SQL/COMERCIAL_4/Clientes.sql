SELECT
    CODIGO,
    NOME AS NOME,
    RAZAO AS FANTASIA,
    APELIDO AS CONTATO,
    case when CPF = '' or CPF is null then CPF else CPF END AS CNPJ_CNPF,
    case when CARTEIRA = '' or CARTEIRA is null then INSCRICAO else CARTEIRA END AS IE_RG,
    INSCRICAO_MUNICIPAL AS IM,
    ENDERECO AS ENDERECO,
    NUMERO AS NUMERO,
    COMPLEMENTO AS COMPLEMENTO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    CEP AS CEP,
    ESTADO AS UF,
    FONE AS TELEFONE,
    FONE2,
    FAX AS FAX,
    CELULAR AS CELULAR,
    EMAIL AS EMAIL,
    OBSERVACAO AS OBSERVACOES,
    LIMITECREDITO AS LIMITE_CREDITO,
    DATAANIVER AS NASCIMENTO,
    DATACADASTRO AS CADASTRO,
    DATAULTIMACOMPRA AS ULTIMA_COMPRA,
    SEXO AS SEXO
FROM CLIENTE
