SELECT
    C.CODIGO AS CODIGO,
    C.NOME AS NOME,
    C.FANTASIA AS FANTASIA,
    C.CONTATO AS CONTATO,
    case when C.RG = '' or C.RG is null then C.INSCRICAO else C.RG END AS IE_RG,
    C.CPFCNPJ AS CNPJ_CNPF,
    C.RUA AS ENDERECO,
    C.COMPLEMENTO AS COMPLEMENTO,
    C.NUMERO AS NUMERO,
    C.BAIRRO AS BAIRRO,
    C.CIDADE AS CIDADE,
    C.UF AS UF,
    C.CEP AS CEP,
    C.TELEFONE AS TELEFONE,
    C.CELULAR AS CELULAR,
    C.EMAIL AS EMAIL,
    C.PROFISSAO AS PROFISSAO,
    C.LIMITE AS LIMITE_CREDITO,
    C.OBS AS OBSERVACOES
FROM CLIENTES AS C
