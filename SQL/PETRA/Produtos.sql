SELECT
  ESTO0000.CODIGO AS CODIGO,
  GRUP0000.DESCRICAO AS GRUPO,
  SUBG0000.DESCRICAO AS FAMILIA,
  FORN0000.DESCRICAO AS FORNECEDOR,
  ESTO0000.DESCRICAO AS DESCRICAO,
  ESTO0001.ESTOQUE AS QTD,
  ESTO0000.TIPOTRIBUTACAO AS ST,
  ESTO0000.DESCRICAOPDV AS CARACTERISTICAS,
  ESTO0000.UNIDADE AS UND,
  ESTO0000.PESO AS PESO,
  ESTO0000.REFERENCIA AS COD_FABRICANTE,
  ESTO0000.CUSTO AS PRECO_CUSTO,
  ESTO0000.VENDA AS PRECO_VENDA,
  ESTO0000.CODIGOBARRAS AS BARRAS,
  CAST (ESTO0001.ULTENTRADA AS DATE) AS ULTIMA_COMPRA,
  CAST (ESTO0001.ULTSAIDA AS DATE) AS ULTIMA_VENDA,
  CAST (ESTO0000.DATAALTERACAO AS DATE) AS ALTERACAO_PRECO,
  CLAS0000.DESCRICAO AS COD_NCM,
  CLAS0000.CEST AS PERSONAL6
FROM ESTO0000
LEFT JOIN ESTO0001 ON ESTO0001.CODIGO = ESTO0000.CODIGO
LEFT JOIN CLAS0000 ON CLAS0000.CODIGO = ESTO0000.CLASSIFFISCAL
LEFT JOIN GRUP0000 ON GRUP0000.CODIGO = ESTO0000.GRUPO
LEFT JOIN SUBG0000 ON SUBG0000.CODIGO = ESTO0000.SUBGRUPO
LEFT JOIN FORN0000 ON FORN0000.CODIGO = ESTO0000.FORNECEDOR
