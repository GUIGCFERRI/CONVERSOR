SELECT
ADM_CLIENTES.CODIGO AS CODIGO,
ADM_CLIENTES.NOME AS NOME,
ADM_CLIENTES.FANTASIA AS FANTASIA,
case when ADM_CLIENTES.CNPJ = '' or ADM_CLIENTES.CNPJ is null then ADM_CLIENTES.CPF else ADM_CLIENTES.CNPJ END AS CNPJ_CNPF,
case when ADM_CLIENTES.IE = '' or ADM_CLIENTES.IE is null then ADM_CLIENTES.RG else ADM_CLIENTES.IE END AS IE_RG,
ADM_CLIENTES.CONTATO AS CONTATO,
ADM_CLI_ENDERECOS.ENDERECO AS ENDERECO,
ADM_CLI_ENDERECOS.NUMERO AS NUMERO,
ADM_CLI_ENDERECOS.COMPLEMENTO AS COMPLEMENTO,
ADM_CLI_ENDERECOS.BAIRRO AS BAIRRO,
ADM_CLI_ENDERECOS.CIDADE AS CIDADE,
ADM_CLI_ENDERECOS.UF AS UF,
ADM_CLI_ENDERECOS.CEP AS CEP,
ADM_CLI_ENDERECOS.ENDERECO_COB AS COB_ENDERECO,
ADM_CLI_ENDERECOS.NUMERO_COB AS COB_NUMERO,
ADM_CLI_ENDERECOS.COMPLEMENTO_COB AS COB_COMPLEMENTO,
ADM_CLI_ENDERECOS.BAIRRO_COB AS COB_BAIRRO,
ADM_CLI_ENDERECOS.CIDADE_COB AS COB_CIDADE,
ADM_CLI_ENDERECOS.UF_COB AS COB_UF,
ADM_CLI_ENDERECOS.CEP_COB AS COB_CEP,
ADM_CLIENTES.TELEFONE AS TELEFONE,
ADM_CLIENTES.CELULAR AS CELULAR,
ADM_CLIENTES.FAX AS FAX,
substring(ADM_clientes.observacoes from 1 for 1000) AS OBSERVACOES,
ADM_CLI_ADICIONAIS.EMAIL AS EMAIL,
COALESCE(ADM_CLIENTES.LIMITE_CREDITO, 0) AS LIMITE_CREDITO,
ADM_CLIENTES.DIA_ACERTO AS DIA_DE_ACERTO,
COALESCE(ADM_CLIENTES.A_RECEBER, 0) AS VALOR_A_RECEBER,
COALESCE(ADM_CLIENTES.ATRASADO, 0) AS VALOR_EM_ATRASO,
COALESCE(ADM_CLI_ADICIONAIS.DATA_CADASTRO, CURRENT_DATE) AS CADASTRO,
COALESCE(ADM_CLI_ADICIONAIS.ULTIMA_VENDA, CURRENT_DATE) AS ULTIMA_VENDA,
ADM_CLI_ADICIONAIS.PROFISSAO AS PROFISSAO,
ADM_CLI_ADICIONAIS.CONJUGE AS CONJUGE,
ADM_CLI_ADICIONAIS.NATURALIDADE AS NATURALIDADE,
ADM_CLI_ADICIONAIS.nascimento AS nascimento,
ADM_CLI_ADICIONAIS.nome_pai AS pai,
ADM_CLI_ADICIONAIS.nome_mae AS mae
FROM ADM_CLIENTES
left JOIN ADM_CLI_ENDERECOS ON ADM_CLIENTES.CODIGO = ADM_CLI_ENDERECOS.CODIGO
left JOIN ADM_CLI_ADICIONAIS ON ADM_CLIENTES.CODIGO = ADM_CLI_ADICIONAIS.CODIGO
WHERE NOME <> '' AND ADM_CLIENTES.CODIGO <> '0'
ORDER BY CODIGO;