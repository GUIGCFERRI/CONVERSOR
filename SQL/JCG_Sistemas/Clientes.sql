SELECT
    CODIGO_CLIENTES AS CODIGO,
    NOME AS NOME,
    APELIDO AS FANTASIA,
    ENDERECO AS ENDERECO,
    NUMERO AS NUMERO,
    BAIRRO AS BAIRRO,
    COMPLEMENTO AS COMPLEMENTO,
    CIDADE AS CIDADE,
    ESTADO AS UF,
    CEP AS CEP,
    RG AS IE_RG,
    case when CPF = '' or CPF is null then CPF_CNPJ else CPF END AS CNPJ_CNPF,
    TELEFONE AS TELEFONE,
    CELULAR AS CELULAR,
    EMAIL AS EMAIL,
    CAST (DATA_CADASTRO AS DATE) AS CADASTRO,
    CAST (DATA_NASCIMENTO AS DATE) AS NASCIMENTO,
    LIMITE_DISPONIVEL AS LIMITE_CREDITO,
    OBSERVACOES AS OBSERVACOES
FROM CLIENTES