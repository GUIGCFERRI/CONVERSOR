SELECT
    SUBSTRING (PRODUTOS.CODIGO FROM 5 FOR 10) AS CODIGO,
    PRODUTOS.NOME AS DESCRICAO,
    PRODUTOS.CODBARRA AS BARRAS,
    PRODUTOS.REFERENCIA AS COD_FABRICANTE,
    PRODUTOS.PESO_LIQUIDO AS PESO,
    PRODUTOS.UNIDADE AS UND,
    PRODUTOS.GRUPO AS GRUPO,
    PRODUTOS.CUSTO_UNIT AS PRECO_CUSTO,
    PRODUTOS.CUSTO_MEDIO AS CUSTO_MEDIO,
    PRODUTOS.MARGEM AS MARGEM_LUCRO,
    PRODUTOS.PRECO_VENDA AS PRECO_VENDA,
    PRODUTOS.PRECO_ATACADO AS PRECO_ATACADO,
    PRODUTOS.ESTOQUE_MINIMO AS QTD_IDEAL,
    PRODUTOS.ESTOQUE_ATUAL AS QTD,
    PRODUTOS.CSOSN_ID AS ST,
    PRODUTOS.NCM AS COD_NCM,
    PRODUTOS.OBS AS OBSERVACOES,
    PRODUTOS.CEST AS PERSONAL6,
    PRODUTOS.CADASTRO AS DATA_CADASTRO,
    CAST (PRODUTOS.ULTIMO_VENDA AS DATE) AS ULTIMA_VENDA,
    PRODUTOS.ULTIMA_ALTERACAO AS ALTERACAO_PRECO
FROM PRODUTOS
