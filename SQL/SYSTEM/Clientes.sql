SELECT
    CLIENTE.CDCLIENTE AS CODIGO,
    CLIENTE.CLIENTENOME AS NOME,
    CLIENTE.CLIENTERAZAOSOCIAL AS FANTASIA,
    CLIENTE.CLIENTESEXO AS SEXO,
    CAST (CLIENTE.CLIENTENASCIMENTO AS DATE) AS NASCIMENTO,
    CAST (CLIENTE.CLIENTEDATACADASTRO AS DATE) AS CADASTRO,
    CLIENTE.CLIENTEESTADOCIVIL AS EST_CIVIL,
    CLIENTE.CLIENTERG AS IE_RG,
    CLIENTE.CLIENTECPF AS CNPJ_CNPF,
    CLIENTE.CLIENTEINSCMUNICIPAL AS IM,
    CLIENTE.CLIENTECEP AS CEP,
    CLIENTE.CLIENTEENDERECO AS ENDERECO,
    --BAIRRO.DESCRICAO AS BAIRRO,
    CLIENTE.CLIENTEREFERENCIA AS COMPLEMENTO,
    CLIENTE.CLIENTEBAIRRODESCRICAO AS BAIRRO,
    CLIENTE.CLIENTECIDADE AS CIDADE,
    CLIENTE.CLIENTEUF AS UF,
    CLIENTE.CLIENTETELEFONERESI AS TELEFONE,
    CLIENTE.CLIENTETELEFONECELULAR AS CELULAR,
    CLIENTE.CLIENTEFAX AS FAX,
    CLIENTE.CODIGOINTERNO,
    CLIENTE.CONJUGENOME AS CONJUGE,
    CLIENTE.PAISNOMEPAI AS PAI,
    CLIENTE.PAISNOMEMAE AS MAE
FROM CLIENTE
--LEFT JOIN BAIRRO ON BAIRRO.CDBAIRRO = CLIENTES.CDBAIRRO