SELECT
P.CODIGO,
P.CODBARRA AS BARRAS,
P.REFERENCIA AS COD_FABRICANTE,
G.descricao AS GRUPO,
P.UNIDADE AS UND,
P.ULTFORN AS FORNECEDOR,
P.LOCALIZACAO AS PERSONAL1,
P.ALIQ_ICM AS ELO,
P.ALIQ_PIS AS PIS_ALIQ_NOR,
P.ALIQ_COF AS COFINS_ALIQ_NOR,
P.PR_CUSTO AS PRECO_CUSTO,
P.MARGEM AS MARGEM_LUCRO,
P.PR_VENDA AS PRECO_VENDA,
P.QTD_ATUAL AS QTD,
P.QTD_MIN AS QTD_IDEAL,
P.CSTIPI AS IPI_CODIGO_VENDA,
P.CSOSN AS ST,
P.NCM AS COD_NCM,
P.COMISSAO,
P.CFOP AS CF,
P.PRECO_ATACADO AS PRECO_ATACADO,
P.QTD_ATACADO AS QTD_PRECO_ATACADO,
P.ALIQ_IPI AS ALIQ_IPI_VENDA,
P.CEST AS PERSONAL6,
P.PESO,
P.FCP,
P.DT_CADASTRO AS DATA_CADASTRO,
P.DESCRICAO,
P.ORIGEM AS OST,
P.ANP AS COD_ANP,
P.SUBGRUPO AS FAMILIA,
P.COD_ENQ_IPI AS ENQIPI_ID
FROM PRODUTO P
LEFT JOIN GRUPO G ON G.codigo = P.grupo
LEFT JOIN subgrupo SB ON SB.codigo = P.subgrupo
ORDER BY P.CODIGO;