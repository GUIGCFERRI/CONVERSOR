SELECT
    replace(P.CODIGO,'.','') AS CODIGO,
    P.CODIGOBARRA AS BARRAS,
    P.DESCRICAO AS DESCRICAO,
    P.EMBALAGEM AS UND,
    P.ESTOQUE AS QTD, 
    P.ESTQMIN AS QTD_IDEAL,
    P.PRECOCUSTO AS PRECO_CUSTO,
    P.PROMARGEMLUCRO AS MARGEM_LUCRO,
    P.PRD_PRECOAVISTA AS PRECO_VENDA,
    P.OBS AS OBSEFVACOES,
    P.ALIQUOTA AS ST,
    replace(P.PRD_CLASSIFICACAO_FISCAL,'.','') AS COD_NCM,
    P.PRD_CEST AS PERSONAL6,
    P.DTCADASTRO AS DATA_CADASTRO,
    P.DTULTIMAENTRADA AS ULTIMA_COMPRA,
    P.DATAULTIMASAIDA AS ULTIMA_VENDA,
    P.DTULTIMAALTERACAO AS ALTERACAO_PRECO,
    case when P.PRODATIVO = 'True' then 'Ativo' else 'Inativo' end as SITUACAO
FROM SMPRODUTO P
