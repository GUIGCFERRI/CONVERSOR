SELECT
	P.T006_CODIGO_PRODUTO AS CODIGO,
	P.T006_COD_BARRA_EMB AS BARRAS,
	P.T006_COD_BARRA_CAIXA AS PERSONAL2,
	P.T006_COD_PERSONALIZADO AS PERSONAL4,
	P.T006_COD_REFERENCIA AS COD_FABRICANTE,
	P.T006_NOME_PRODUTO AS DESCRICAO,
	CASE P.T006_INDICADOR_ATIVACAO WHEN 'A' THEN 'Ativo' ELSE 'Inativo' END AS SITUACAO,
	P.T006_VALOR_CUSTO AS PRECO_CUSTO,
	P.T006_VALOR_CUSTO_MEDIO AS CUSTO_MEDIO,	
	P.T006_MARGEM_LUCRO AS MARGEM_LUCRO,	
	P.T006_VALOR_DE_VENDA AS PRECO_VENDA,
	P.T006_QT_MINIMA AS QTD_IDEAL,
	CONCAT (P.T006_OBSERVACOES,' - ',P.T006_INFORMACOES_PRODUTO) AS OBSERVACOES,	
	P.T006_DT_ULT_COMP AS ULTIMA_COMPRA,
	P.T006_DT_CADASTRO AS DATA_CADASTRO,
	P.T006_PESO AS PESO,
	P.T006_NCM AS COD_NCM,
	P.T006_PC_COMISSAO AS COMISSAO,
	P.T006_QT_ESTOQUE_LOJA01 AS QTD,
	P.T006_QT_ESTOQUE_LOJA02 AS PERSONAL5,
	P.T006_QTDE_EMBALAGEM AS FAT_CONV,		
	P.T006_ORIGEM_MERC_ICMS AS OST,
	P.T006_TAMANHO AS TAMANHO,
	P.T006_QTDE_ATACAREJO AS QTD_PRECO_ATACADO,
	P.T006_VR_ATACAREJO AS PRECO_ATACADO,	
	P.T006_VALIDADE_BALANCA AS VALIDADE_DIAS,	
	P.T006_COD_CEST_T325 AS PERSONAL6,
	P.T006_COFINS AS COFINS_ALIQ_NOR,
	P.T006_PIS AS PIS_ALIQ_NOR,
	T006_IPI AS ALIQ_IPI_VENDA,		
 	GG.T063_NOME_GRUPO AS GRUPO,
 	GS.T064_NOME_SUBGRUPO AS FAMILIA,
	GM.T110_DESC_MARCA AS PERSONAL1,
	GD.T062_NOME_DEPARTAMENTO AS CARACTERISTICAS,
	UN.T061_SIGLA_UNIDADE AS UND,
	UNC.T061_SIGLA_UNIDADE AS UND_COMPRA,
	CS.T185_CST_ICMS AS ST,
	CC.T182_CST_COFINS AS COFINS_CODIGO,
	CP.T183_CST_PIS AS PIS_CODIGO,
	IP.T184_CST_IPI AS IPI_CODIGO_VENDA
FROM
	GCOTB006_PRODUTO P
	LEFT JOIN GCOTB063_GRUPO GG ON GG.T063_CODIGO_GRUPO = P.T006_CODIGO_GRUPO_T063 
	LEFT JOIN GCOTB064_SUBGRUPO GS ON GS.T064_COD_SUBGRUPO = P.T006_COD_SUBGRUPO_T064 AND GS.T064_CODIGO_GRUPO_T063 = GG.T063_CODIGO_GRUPO 
	LEFT JOIN GCOTB110_MARCA GM ON GM.T110_COD_MARCA = P.T006_COD_MARCA_T110 
	LEFT JOIN GCOTB062_DEPARTAMENTO GD ON GD.T062_CODIGO_DEPARTAMENTO = P.T006_COD_DEPARTAMENTO_T062
	LEFT JOIN GCOTB061_UNID_MEDIDA UN ON UN.T061_CODIGO_MEDIDA = P.T006_CODIGO_UNIDADE_T061
	LEFT JOIN GCOTB061_UNID_MEDIDA UNC ON UNC.T061_CODIGO_MEDIDA = P.T006_CODIGO_UNID_COMPRA_T061
	LEFT JOIN GCOTB185_CST_ICMS CS ON CS.T185_COD_ICMS = P.T006_COD_ICMS_T185
	LEFT JOIN GCOTB182_CST_COFINS CC ON CC.T182_COD_COFINS = P.T006_COD_COFINS_T182
	LEFT JOIN GCOTB183_CST_PIS CP ON CP.T183_COD_PIS = P.T006_COD_PIS_T183
	LEFT JOIN GCOTB184_CST_IPI IP ON IP.T184_COD_IPI = P.T006_COD_IPI_T184