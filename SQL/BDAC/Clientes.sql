SELECT
    COD_CLIENTE AS CODIGO,
    NOME AS NOME,
    RAZAO_SOCIAL AS FANTASIA,
    CGC_CPF AS CNPJ_CNPF,
    INSC_MUNICIPAL AS IM,
    case when IDENTIDADE = '' or IDENTIDADE is null then INSCRICAO_ESTADUAL else IDENTIDADE END AS IE_RG,
    ENDERECO_1 AS ENDERECO,
    NUMERO AS NUMERO,
    BAIRRO_1 AS BAIRRO,
    COMPL AS COMPLEMENTO,
    CIDADE_1 AS CIDADE,
    UF_1 AS UF,
    CEP_1 AS CEP,
    EMAIL_1 AS EMAIL,
    TELEFONE_1 AS TELEFONE,
    CELULAR_1 AS CELULAR,
    CONJUGE AS CONJUGE,
    ESTADO_CIVIL AS EST_CIVIL,
    OBSERVACAO AS OBSERVACOES,
    NATURALIDADE AS NATURALIDADE,
    DATA_CADASTRO AS CADASTRO,
    CLI_PAI AS PAI,
    CLI_MAE AS MAE,
    NASCIMENTO AS NASCIMENTO,
    CREDITO AS LIMITE_CREDITO
FROM CLIENTES
WHERE COD_CLIENTE <> ''