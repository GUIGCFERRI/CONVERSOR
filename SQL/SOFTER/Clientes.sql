SELECT
    TCLIENTE.CONTROLE AS CODIGO,
    TCLIENTE.CLIENTE AS NOME,
    TCLIENTE.FANTASIA AS FANTASIA,
    case when TCLIENTE.CPF = '' or TCLIENTE.CPF is null then TCLIENTE.CNPJ else TCLIENTE.CPF END AS CNPJ_CNPF,
    case when TCLIENTE.RG = '' or TCLIENTE.RG is null then TCLIENTE.IE else TCLIENTE.RG END AS IE_RG,
    TCLIENTE.IM AS IM,
    TCLIENTE.ENDERECO AS ENDERECO,
    TCLIENTE.NUMERO AS NUMERO,
    TCLIENTE.COMPLEMENTO AS COMPLEMENTO,
    TCLIENTE.BAIRRO AS BAIRRO,
    TCLIENTE.CIDADE AS CIDADE,
    TCLIENTE.UF AS UF,
    TCLIENTE.CEP AS CEP,
    TCLIENTE.TELEFONE AS TELEFONE,
    TCLIENTE.CELULAR AS CELULAR,
    TCLIENTE.EMAIL AS EMAIL,
    TCLIENTE.PAI AS PAI,
    TCLIENTE.MAE AS MAE,
    TCLIENTE.OBS AS OBSERVACOES,
    TCLIENTE.ESTADOCIVIL AS EST_CIVIL,
    TCLIENTE.LIMITECREDITO AS LIMITE_CREDITO,
    TCLIENTE.NOMECONJUGE AS CONJUGE,
    TCLIENTE.SEXO AS SEXO,
    CAST (TCLIENTE.DATANASCIMENTO AS DATE) AS NASCIMENTO,
    CAST (TCLIENTE.DATAHORACADASTRO AS DATE) AS CADASTRO,
    TCLIENTE.DATAULTIMAVENDA AS ULTIMA_VENDA
FROM TCLIENTE
