SELECT FICHA001.CODIGO,
FICHA001.NOME,
FICHA001.ENDERECO,
FICHA001.BAIRRO,
FICHA001.CEP,
FICHA001.TELEFONE,
FICHA001.INSCRICAO AS IE_RG,
FICHA001.CGC AS CNPJ_CNPF,
FICHA001.OBSERVACAO AS OBSERVACOES,
FICHA001.NUMERO,
CIDADE.NOME_CID AS CIDADE,
CIDADE.UF,
FICHA001.EMAIL,
FICHA001.APELIDO AS FANTASIA,
FICHA001.DESDE AS CADASTRO,
FICHA001.COMPLEMENTO
FROM FICHA001
LEFT JOIN CIDADE ON FICHA001.ID_CID=CIDADE.ID_CID

