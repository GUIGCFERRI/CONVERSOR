SELECT
  SAU1007.CODPRO AS CODIGO,
  SAU1007.DESPRO AS DESCRICAO,
  SAU1007.DESPRO_2 AS CARACTERISTICAS,
  SAU1007.REFPRO AS COD_FABRICANTE,
  SAU1006.NOMGRU AS GRUPO,
  SAU1001.NOMFOR AS FORNECEDOR,
  SAU1007.CODBAR AS BARRAS,
  SAU1007.UNISAI_1 AS UND,
  SAU1007.ESTMIN AS QTD_IDEAL,
  SAU1007.ESTATU AS QTD,
  SAU1007.PESLIQ AS PESO,
  SAU1007.PRECUS AS PRECO_CUSTO,
  SAU1007.PREATA AS PRECO_ATACADO,
  SAU1007.PREVAR AS PRECO_VENDA,
  SAU1007.OBSPRO AS OBSERVACOES,
  SAU1007.CLAFIS AS COD_NCM,
  SAU1007.CEST AS PERSONAL6,
  SAU1007.TRI002 AS ST,
  SAU1007.DATCAD AS DATA_CADASTRO,
  SAU1007.DATAJU AS ALTERACAO_PRECO,
  SAU1007.PERLUC AS MARGEM_LUCRO
FROM SAU1007
LEFT JOIN SAU1006 ON SAU1006.CODGRU = SAU1007.CODGRU
LEFT JOIN SAU1001 ON SAU1001.CODFOR = SAU1007.CODFOR