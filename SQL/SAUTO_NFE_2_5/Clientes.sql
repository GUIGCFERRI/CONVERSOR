SELECT
  SAU1000.CODCLI AS CODIGO,
  SAU1000.NOMCLI AS NOME,
  SAU1000.NOMFAN AS FANTASIA,
  SAU1000.DATCAD AS CADASTRO,
  SAU1000.CPFCNP AS CNPJ_CNPF,
  SAU1000.IDECLI AS IE_RG,
  SAU1000.ENDCLI AS ENDERECO,
  SAU1000.NUMCLI AS NUMERO,
  SAU1000.COMCLI AS COMPLEMENTO,
  SAU1000.BAICLI AS BAIRRO,
  SAU1012.NOMCID AS CIDADE,
  SAU1012.ESTCID AS UF,
  SAU1000.CEPCLI AS CEP,
  SAU1000.FONCLI AS TELEFONE,
  SAU1000.CELCLI AS CELULAR,
  SAU1000.EMACLI AS EMAIL,
  SAU1000.NOMMAE AS NAE,
  SAU1000.NOMPAI AS PAI,
  SAU1000.OBSCLI AS OBSERVACOES,
  SAU1000.DATNAS AS NASCIMENTO,
  SAU1000.CONTAT AS CONTATO,
  SAU1000.LIMCRE AS LIMITE_CREDITO
FROM SAU1000
LEFT JOIN SAU1012 ON SAU1012.CODCID = SAU1000.CODCID