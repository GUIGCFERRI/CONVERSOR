SELECT
    CLIENTES.ID AS CODIGO,
    CLIENTES.NOME,
    CLIENTES.CPF AS CNPJ_CNPF,
    CLIENTES.IDENTIDADE AS IE_RG,
    CLIENTES.ENDERECO,
    CLIENTES.BAIRRO,
    CLIENTES.CIDADE,
    CLIENTES.UF,
    CLIENTES.CEP,
    CLIENTES.TELEFONE,
    CLIENTES.CELULAR,
    CLIENTES.EMAIL,
    CLIENTES.FANTASIA,
    CLIENTES.NASCIMENTO,
    CLIENTES.FAX,
    CLIENTES.DATA AS CADASTRO,
    CLIENTES.NUMERO,
    CLIENTES.COMPLEMENTO,
    CLIENTES.SEXO,
    CLIENTES.PAI,
    CLIENTES.MAE,
    CLIENTES.OBSERVACAO AS OBSERVACOES
FROM CLIENTES
WHERE CLIENTES.ID > 0
