SELECT
    CLIENTES.CODIGO_CLIENTE AS CODIGO,
    CLIENTES.NOME AS NOME,
    CLIENTES.FANTASIA AS FANTASIA,
    CLIENTES.CNPJ_CPF AS CNPJ_CNPF,
    case when CLIENTES.INSCRICAO = '' or CLIENTES.INSCRICAO is null then CLIENTES.NUMERO_IDENT else CLIENTES.INSCRICAO END as IE_RG,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.COMPLEMENTO AS COMPLEMENTO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.CEP AS CEP,
    CLIENTES.UF AS UF,
    CLIENTES.NASCIMENTO AS NASCIMENTO,
    CLIENTES.FONE AS TELEFONE,
    CLIENTES.FAX AS FAX,
    CLIENTES.CELULAR AS CELULAR,
    CLIENTES.CONTATO AS CONTATO,
    CLIENTES.NATURALIDADE AS NATURALIDADE,
    CLIENTES.ESTADO_CIVIL AS EST_CIVIL,
    CLIENTES.PAI AS PAI,
    CLIENTES.MAE AS MAE,
    CLIENTES.CONJUGE AS CONJUGE,
    CLIENTES.ENDERECO_COBRANCA AS COB_ENDERECO,
    CLIENTES.BAIRRO_COBRANCA AS COB_BAIRRO,
    CLIENTES.CIDADE_COBRANCA AS COB_CIDADE,
    CLIENTES.CEP_COBRANCA AS COB_CEP,
    CLIENTES.UF_COBRANCA AS COB_UF,
    CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO,
    CLIENTES.OBS AS OBSERVACOES,
    CLIENTES.DATA_CAD AS CADASTRO,
    CLIENTES.INSCRICAO_MUNICIPAL AS IM,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.NUMERO AS NUMERO
FROM CLIENTES
