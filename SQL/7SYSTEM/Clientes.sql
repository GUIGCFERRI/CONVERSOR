SELECT
  clientes.CODIGO AS CODIGO,
  clientes.NOME AS NOME,
  clientes.FANTASIA AS FANTASIA,
  clientes.ENDERECO AS ENDERECO,
  clientes.BAIRRO AS BAIRRO,
  CEPBR_CIDADE.CIDADE AS CIDADE,
  CEPBR_CIDADE.UF AS UF,
  clientes.CEP AS CEP,
  clientes.TELEFONE AS TELEFONE,
  clientes.FAX AS FAX,
  clientes.CPF_CNPJ AS CNPJ_CNPF,
  clientes.IE_RG AS IE_RG,
  clientes.COMPLENT AS COMPLEMENTO,
  clientes.NOMEPAI AS PAI,
  clientes.NOMEMAE AS MAE,
  clientes.LMTCRED AS LIMITE_CREDITO,
  clientes.EMAIL AS EMAIL,
  clientes.SEXO AS SEXO,
  clientes.OBSERVACAO AS OBSERVACOES,
  clientes.ESTADOCV AS EST_CIVIL,
  clientes.CONJUGE AS CONJUGE,
  clientes.NUMERO AS NUMERO
FROM clientes
LEFT JOIN CEPBR_CIDADE ON CEPBR_CIDADE.id_cidade = CLIENTES.LKCIDADE
