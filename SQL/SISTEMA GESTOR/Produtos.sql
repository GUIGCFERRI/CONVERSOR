SELECT 
PRODUTO.CODIGO AS CODIGO,
PRODUTO.DESCRICAO AS DESCRICAO,
PRODUTO.CODBARRA AS BARRAS,
PRODUTO.REFERENCIA AS COD_FABRICANTE,
GRUPO.DESCRICAO AS GRUPO,
MARCA.DESCRICAO AS FAMILIA,
PRODUTO.UNIDADE AS UND,
PRODUTO.PR_CUSTO AS PRECO_CUSTO,
PRODUTO.MARGEM AS MARGEM_LUCRO,
PRODUTO.PR_VENDA AS PRECO_VENDA,
PRODUTO.QTD_ATUAL AS QTD,
PRODUTO.QTD_MIN AS QTD_IDEAL,
PRODUTO.CSOSN AS ST,
PRODUTO.NCM AS COD_NCM,
PRODUTO.PRECO_ATACADO AS PRECO_ATACADO,
PRODUTO.CEST AS PERSONAL6,
PRODUTO.TIPO AS PERSONAL1,
PRODUTO.ALIQ_IPI AS ALIQ_IPI_VENDA,
PRODUTO.ALIQ_PIS AS PIS_ALIQ_NOR,
PRODUTO.ALIQ_COF AS COFINS_ALIQ_NOR,
PRODUTO.CSTIPI AS IPI_CODIGO_VENDA,
PRODUTO.CSTE AS PIS_CODIGO,
PRODUTO.CSTS AS COFINS_CODIGO,
PRODUTO.COMISSAO,
PRODUTO.PESO,
PRODUTO.DT_CADASTRO AS DATA_CADASTRO,
PRODUTO.ORIGEM AS OST,
--PRODUTO.DIAS_VALIDADE AS VALIDADE_DIAS,
LEFT (PRODUTO.CFOP,3) AS ELO, 
CASE PRODUTO.ATIVO WHEN 'S' THEN 'Ativo' ELSE 'Inativo' END AS SITUACAO
FROM PRODUTO
LEFT JOIN GRUPO ON GRUPO.CODIGO = PRODUTO.GRUPO
LEFT JOIN MARCA ON MARCA.CODIGO = PRODUTO.FK_MARCA
