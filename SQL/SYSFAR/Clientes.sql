SELECT
  CODIGO,
  NOME,
  NOMEC AS FANTASIA,
  ENDERECO,
  BAIRRO,
  CIDADE,
  CEP,
  REPLACE (ESTADO,'.','') AS UF,
  COMPLEMENT AS COMPLEMENTO,
  CIC AS CNPJ_CNPF,
  RG AS IE_RG,
  INSCR_MUN AS IM,
  CONCAT(IFNULL(DDD, ''),IFNULL(TELEFONE, '')) AS TELEFONE,
  TELEF2 AS CELULAR,
  OBS AS OBSERVACOES,
  NASCIMENTO,
  CONJUGE,
  LIMITE AS LIMITE_CREDITO,
  EMAIL,
  NUMERO,
  DATA AS CADASTRO,
  CONTATO,
  SEXO,
  ULTCOMPRA AS ULTIMA_VENDA
FROM CLIENTES