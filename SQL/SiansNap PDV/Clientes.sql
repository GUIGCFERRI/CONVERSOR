SELECT
    C.CLI_CODIGO AS CODIGO,
    C.CLI_NOME AS NOME,
    C.CLI_APELIDO AS FANTASIA,
    case when C.CLI_CNPJ = '' or C.CLI_CNPJ is null then C.CLI_CPF else C.CLI_CNPJ END AS CNPJ_CNPF,
    case when C.CLI_IE = '' or C.CLI_IE is null then C.CLI_RG else C.CLI_IE END AS IE_RG,
    C.CLI_LOGRADOURO AS ENDERECO,
    C.CLI_NR_LOG AS NUMERO,
    C.CLI_COMPLEMENTO AS COMPLEMENTO,
    C.CLI_BAIRRO AS BAIRRO,
    C.CLI_CEP AS CEP,
    M.MUN_NOME AS CIDADE,
    M.MUN_UF AS UF,
    C.CLI_TELEFONE AS TELEFONE,
    C.CLI_FAX AS FAX,
    C.CLI_EMAIL AS EMAIL,
    C.CLI_PAI AS PAI,
    C.CLI_MAE AS MAE,
    C.CLI_SEXO AS SEXO,
    C.CLI_ESTADOCIVIL AS EST_CIVIL,
    C.CLI_CONJUGE AS CONJUGE,
    C.CLI_PROFISSAO AS PROFISSAO,
    C.CLI_LIMITE AS LIMITE_CREDITO,
    C.CLI_DTANIVER AS NASCIMENTO,
    C.CREATE_DATE AS CADASTRO,
    C.OBS AS OBSERVACOES,
    case when C.CLI_ATIVO = 'S' then 'Ativo' else 'Inativo' end as situacao
FROM CLIENTES AS C
LEFT JOIN MUNICIPIO M ON C.MUN_CODIGO = M.MUN_CODIGO