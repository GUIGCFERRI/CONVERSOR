SELECT
	CC.ID_CLIENTE AS CODIGO,
	CC.DESCRICAO AS  NOME,
	CC.FANTASIA,	
	CC.CPF_CNPJ AS CNPJ_CNPF,
	CASE WHEN CC.RG_NUM = '' OR CC.RG_NUM IS NULL THEN CC.IE ELSE CC.RG_NUM END AS IE_RG,
	CC.IM,	
	CC.ENDERECO,
	CC.CEP,
	CC.BAIRRO,
	CC.COMPLEMENTO,
	CC.NUMERO,
	CC.FONE AS TELEFONE,
	CC.CELULAR,
	CC.FAX,
	CC.DT_CADASTRO AS CADASTRO,
	CC.EST_CIVIL,
	CC.SEXO,
	CC.CONJUGE,
	CC.DT_NASCTO AS NASCIMENTO,
	CC.NOME_PAI AS PAI,
	CC.NOME_MAE AS MAE,
	CC.EMAIL,
	CC.INF_GERAIS AS OBSERVACOES,
--	CC.OBS_SOMENTE_AVISTA AS OBSERVACOES,	
	CC.LIMITE_CREDITO,
	CC.PROFISSAO,
	CM.DESCRICAO  AS CIDADE,
	CM.UF 
FROM
	CAD_CLIENTE CC
LEFT JOIN CAD_MUNICIPIO CM ON CM.ID_MUNICIPIO = CC.ID_MUNICIPIO 