SELECT FORNECEDOR.Codigo,
FORNECEDOR.Descricao AS NOME,
substring (FORNECEDOR.[Cgc ou Cpf],1,18) as CNPJ_CNPF,
FORNECEDOR.Fone AS TELEFONE,
FORNECEDOR.Endereco AS ENDERECO,
FORNECEDOR.Bairro,
FORNECEDOR.Cep,
FORNECEDOR.Cidade,
FORNECEDOR.Uf,
substring (FORNECEDOR.Rg,1,20) AS IE_RG,
FORNECEDOR.Codigo,
FORNECEDOR.Fax,
FORNECEDOR.Fantasia,
FORNECEDOR.Celular,
FORNECEDOR.Email
FROM FORNECEDOR
