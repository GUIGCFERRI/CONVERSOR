SELECT
	F.T005_CODIGO_FORNECEDOR AS CODIGO,
	F.T005_RAZAO_SOCIAL AS NOME,
	F.T005_NOME_FANTASIA AS FANTASIA,
	F.T005_EMAIL AS EMAIL,
	F.T005_CNPJ AS CNPJ_CNPF,
	F.T005_NOME_CONTATO AS CONTATO,
	F.T005_OBSERVACOES AS OBSERVACOES,
	F.T005_ENDERECO AS ENDERECO,
	F.T005_BAIRRO AS BAIRRO,
	F.T005_CIDADE AS CIDADE,
	F.T005_PONTO_DE_REFERENCIA AS COMPLEMENTO,
	F.T005_CEP AS CEP,
	F.T005_NUMERO_NFE AS NUMERO,
	F.T005_TELEFONE_01 AS TELEFONE,
	F.T005_TELEFONE_02 AS PERSONAL2,
	F.T005_FAX AS FAX,
	F.T005_CELULAR AS CELULAR,
	ES.T050_SIGLA_UF AS UF
FROM
	GCOTB005_FORNECEDOR F
LEFT JOIN GCOTB050_ESTADO ES ON ES.T050_CODIGO_ESTADO = F.T005_CODIGO_ESTADO_T050