SELECT 
CADPROD.CODPROD AS CODIGO,
CADPROD.DESCRICAO,
CADPROD.PCUSTO AS PRECO_CUSTO,
CADPROD.PUNIT AS PRECO_VENDA,
CADPROD.CUSMEDIO AS CUSTO_MEDIO,
CADPROD.MARKUP AS MARGEM_LUCRO, 
CADPROD.PRATACADO AS PRECO_ATACADO, 
CADPROD.QTESTOQUE AS QTD,
CADPROD.UND,
CADPROD.EAN13 AS BARRAS,
CADPROD.NOMENCLATURAMERCOSUL AS COD_NCM,
-- CADPROD.CST AS ST,
CADPROD.CSOSN AS ST,
CADPROD.CEST AS PERSONAL6,
CADPROD.DTULTCOMPRA AS ULTIMA_COMPRA, 
CADPROD.DTULTVENDA AS ULTIMA_VENDA, 
CADPROD.PERCOMIS AS COMISSAO,
CADPROD.PESO,
CADPROD.ESTOQUEMINIMO AS QTD_IDEAL,
CADPROD.TAMANHO,
CADPROD.COR, 
CADPROD.OBS AS OBSERVACOES,
CADPROD.CATEGORIA AS PERSONAL1,
CADPROD.MODELO AS PERSONAL2,
CADPROD.LOCALIZACAO AS PERSONAL4,
CADPROD.PERCDEBPISCOFINS AS PISE_ALIQ_NOR, 
CADPROD.PERCDEBPISCOFINS AS COFINSE_ALIQ_NOR, 
CADPROD.PERCDEBIPI AS ALIQ_IPI,
CADPROD.PERCCREDPISCOFINS AS PIS_ALIQ_NOR,
CADPROD.PERCCREDCOFINS AS COFINS_ALIQ_NOR,
CADPROD.PERCIPI AS ALIQ_IPI_VENDA,
-- CADPROD.PERCOFINS,
CADPROD.CSTPISENT AS PISE_CODIGO, 
CADPROD.CSTCOFINSENT AS COFINSE_CODIGO, 
CADPROD.CSTIPIENT AS IPI_CODIGO, 
CADPROD.CSTPISSAI AS PIS_CODIGO, 
CADPROD.CSTCOFINSSAI AS COFINS_CODIGO, 
CADPROD.CSTIPISAI AS IPI_CODIGO_VENDA, 
CADPROD.CFOPVENDA AS CF,
LEFT (CADPROD.CFOPVENDA,3) AS ELO,
CADPROD.CODFABRICANTE AS COD_FABRICANTE,
COALESCE(CADPROD.DTCADASTRO,CURRENT_DATE) AS CADASTRO,
CADMARCAS.MARCA AS FAMILIA,
CADFORNEC.FORNECEDOR AS FORNECEDOR,
CADGRUPO.DESCRICAO AS GRUPO
FROM CADPROD
LEFT JOIN CADFORNEC ON CADFORNEC.CODFORNEC = CADPROD.CODFORNEC
LEFT JOIN CADGRUPO ON CADGRUPO.CODGRUPO = CADPROD.CODGRUPO
LEFT JOIN CADMARCAS ON CADMARCAS.CODMARCA = CADPROD.CODMARCA