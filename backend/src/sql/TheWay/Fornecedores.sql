SELECT 
CADFORNEC.CODFORNEC AS CODIGO,
CADFORNEC.FORNECEDOR AS NOME,
CADFORNEC.FANTASIA,
CADFORNEC.ENDERECO,
CADFORNEC.COMPLEMENTO,
CADFORNEC.NUMERO,
CADFORNEC.BAIRRO,
CADFORNEC.UF,
CADFORNEC.CIDADE,
REPLACE (CADFORNEC.CEP,'.',''),
CASE WHEN CADFORNEC.CPF = '' OR CADFORNEC.CPF IS NULL THEN CADFORNEC.CGC ELSE CADFORNEC.CPF END AS CNPJ_CNPF,
CADFORNEC.IE AS IE_RG,
CADFORNEC.INSC_MUNICIPAL AS IM,
CADFORNEC.TEL1 AS TELEFONE,
CADFORNEC.TEL2 AS CELULAR,
CADFORNEC.TEL3 AS FAX,
CADFORNEC.CONTATO,
CADFORNEC.EMAIL 
FROM CADFORNEC