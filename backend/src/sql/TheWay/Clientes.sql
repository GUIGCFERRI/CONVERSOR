SELECT 
CADCLIENTE.CODCLI AS CODIGO,
CADCLIENTE.CLIENTE AS NOME,
CADCLIENTE.FANTASIA,
CADCLIENTE.ENDERECO,
CADCLIENTE.COMPLEMENTO,
CADCLIENTE.NUMERO,
CADCLIENTE.BAIRRO,
CADCLIENTE.CIDADE,
CADCLIENTE.UF,
REPLACE (CADCLIENTE.CEP,'.','') AS CEP,
CASE WHEN CADCLIENTE.CPF IS NULL OR CADCLIENTE.CPF='' THEN CADCLIENTE.CNPJ ELSE CADCLIENTE.CPF END AS CNPJ_CNPF,
CASE WHEN CADCLIENTE.RG IS NULL OR CADCLIENTE.RG='' THEN CADCLIENTE.IE ELSE CADCLIENTE.RG END AS IE_RG,
CADCLIENTE.TEL1 AS TELEFONE,
CADCLIENTE.TEL2 AS CELULAR,
CADCLIENTE.TEL3 AS FAX,
COALESCE(CADCLIENTE.DTCADASTRO,CURRENT_DATE) AS CADASTRO,
CADCLIENTE.DTNASCIMENTO AS NASCIMENTO,
CADCLIENTE.OBS AS OBSERVACOES,
CADCLIENTE.PAI,
CADCLIENTE.MAE,
CADCLIENTE.CONTATO,
CADCLIENTE.EMAIL,
CADCLIENTE.LIMCRED AS LIMITE_CREDITO,
CADCLIENTE.ESTADOCIVIL AS EST_CIVIL,
CADCLIENTE.CONJUGE, 
CADCLIENTE.PROFISSAO, 
CADCLIENTE.EMPRESA AS LOCTRA
FROM CADCLIENTE