SELECT
    SUBSTRING (CLIENTES.CODIGO FROM 5 FOR 10) AS CODIGO,
    CLIENTES.NOME AS NOME,
    CLIENTES.RAZAO AS FANTASIA,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.NUMERO AS NUMERO,
    CLIENTES.COMPLEMENTO AS COMPLEMENTO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CEP AS CEP,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.ESTADO AS UF,
    CLIENTES.CONTATO AS CONTATO,
    case when CLIENTES.CPF = '' or CLIENTES.CPF is null then CLIENTES.CNPJ else CLIENTES.CPF END AS CNPJ_CNPF,
    case when CLIENTES.RG = '' or CLIENTES.RG is null then CLIENTES.IE else CLIENTES.RG END AS IE_RG,
    CLIENTES.TELEFONE AS TELEFONE,
    CLIENTES.CELULAR AS CELULAR,
    CLIENTES.OBS AS OBSERVACOES,
    CLIENTES.DATANASC AS NASCIMENTO,
    CAST (CLIENTES.DATACADASTRO AS DATE) AS CADASTRO,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.NOME_CONJUGE AS CONJUGE,
    CLIENTES.PAI AS PAI,
    CLIENTES.MAE AS MAE,
    CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO
FROM CLIENTES
