SELECT
    CODIGO AS CODIGO,
    RAZAO AS NOME,
    NOME AS FANTASIA,
    CONTATO AS CONTATO,
    CNPJ AS CNPJ_CNPF,
    RGIE AS IE_RG,
    ENDERECO AS ENDERECO,
    BAIRRO AS BAIRRO,
    NUMERO AS NUMERO,
    COMPLEME AS COMPLEMENTO,
    CIDADE AS CIDADE,
    ESTADO AS UF,
    CEP AS CEP,
    SUBSTRING (EMAIL FROM 1 FOR 60) AS EMAIL,
    DDDFONE || FONE AS TELEFONE,
    DDDCEL || CELULAR AS CELULAR,
    DDDFAX || FONEFAX AS FAX,
    OBSERVAC AS OBSERVACOES,
    case when SITUACAO = 'A' then 'Ativo' else 'Inativo' end as SITUACAO
FROM CADFOR