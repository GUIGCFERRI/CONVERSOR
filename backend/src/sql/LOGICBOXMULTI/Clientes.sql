SELECT
    SRQCLI.COD_CLI AS CODIGO,
    SRQCLI.NOME_CLI AS NOME,
    SRQCLI.FANTAS_CLI AS FANTASIA,
    SRQCLI.CONTAT_CLI AS CONTATO,
    case when SRQCLI.CIC_CLI = '' or SRQCLI.CIC_CLI is null then SRQCLI.CGC_CLI else SRQCLI.CIC_CLI END AS CNPJ_CNPF,
    case when SRQCLI.RG_CLI = '' or SRQCLI.RG_CLI is null then SRQCLI.INSCRI_CLI else SRQCLI.RG_CLI END AS IE_RG,
    SRQCLI.INSCRI_MUNICIPAL AS IM,
    SRQCLI.ENDERE_CLI AS ENDERECO,
    SRQCLI.BAIRRO_CLI AS BAIRRO,
    SRQCLI.NRO_ENDERECO_CLI AS NUMERO,
    SRQCLI.COMPLEMENTO_ENDER_CLI AS COMPLEMENTO,
    SRQCLI.CEP_CLI AS CEP,
    SRQCLI.CIDADE_CLI AS CIDADE,
    SRQCLI.UF_CLI AS UF,
    SRQCLI.FONE_CLI AS TELEFONE,
    SRQCLI.CELULAR AS CELULAR,
    SRQCLI.FAX_CLI AS FAX,
    SRQCLI.EMAIL AS EMAIL,
    SRQCLI.CONJUGE_CLI AS CONJUGE,
    SRQCLI.NOME_MAE AS MAE,
    SRQCLI.NOME_PAI AS PAI,
    SRQCLI.SEXO AS SEXO,
    CAST (SRQCLI.DATA_CLI AS DATE) AS CADASTRO,
    CAST (SRQCLI.DATA_NASC AS DATE) AS NASCIMENTO
FROM SRQCLI 
