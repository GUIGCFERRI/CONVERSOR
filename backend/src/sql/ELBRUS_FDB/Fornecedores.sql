    LOJB057.COD_FORNEC AS CODIGO,
    LOJB057.FANTASIA AS FANNTASIA,
    LOJB057.RAZAO_SOC AS NOME,
    LOJB057.CGC AS CNPJ_CNPF,
    LOJB057.INSC_EST AS IE_RG,
    LOJB209.ENDERECO AS ENDERECO,
    LOJB209.NUMERO_END AS NUMERO,
    LOJB209.COMPLEMENT AS COMPLEMENTO,
    LOJB209.BAIRRO AS BAIRRO,
    LOJB209.CIDADE AS CIDADE,
    LOJB209.ESTADO AS UF,
    LOJB209.CEP AS CEP,
    LOJB057.DDD ||''|| LOJB057.TELEFONE AS TELEFONE,    
    LOJB057.DDDFAX ||''|| LOJB057.FAX AS FAX,
    LOJB057.DDDCELULAR ||''|| LOJB057.CELULAR AS CELULAR
