SELECT
    CLIENTES.CODIGO AS CODIGO,
    CLIENTES.NOME AS NOME,
    CLIENTES.FANTASIA AS FANTASIA,
    CLIENTES.CONTATO AS CONTATO,
    case when CLIENTES.CNPJ = '' or CLIENTES.CNPJ is null then CLIENTES.CPF else CLIENTES.CNPJ END AS CNPJ_CNPF,
    case when CLIENTES.IE = '' or CLIENTES.IE is null then CLIENTES.RG else CLIENTES.IE END AS IE_RG,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.NUMERO AS NUMERO,
    CLIENTES.COMPLEMENTO AS COMPLEMENTO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CEP AS CEP,
    CLIENTES.UF AS UF,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.TELEFONE AS TELEFONE,
    CLIENTES.CELULAR AS CELULAR,
    CLIENTES.FAX AS FAX,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.OBSERVACOES AS OBSERVACOES,
    CLIENTES.NOME_MAE AS MAE,
    CLIENTES.NOME_PAI AS PAI,
    CLIENTES.CONJUGUE AS CONJUGE,
    CLIENTES.PROFISSAO AS PROFISSAO,
    CLIENTES.NASCIMENTO AS NASCIMENTO,
    CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO,   
    CLIENTES.DATA_CADASTRO AS CADASTRO,
    CLIENTES.ULTIMA_COMPRA AS ULTIMA_COMPRA,
    case when ATIVO = '1' then 'Ativo' else 'Inativo' end as SITUACAO
FROM CLIENTES
WHERE CLIENTES.CODIGO > '0'