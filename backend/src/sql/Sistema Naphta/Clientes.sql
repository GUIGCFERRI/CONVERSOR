SELECT
    COD_CLIENTE AS CODIGO,
    case when CPF = '' or CPF is null then CGC else CPF END  AS CNPJ_CNPF,
    INSCRICAO AS IE_RG,
    NOME AS NOME,
    ENDERECO,
    BAIRRO,
    CIDADE,
    UF,
    CEP,
    DATA_CADASTRO AS CADASTRO,
    CONTATO,
    FONE1 AS TELEFONE,
    FONE2 AS CELULAR,
    FAX,
    case when ATIVO = 'S' then 'Ativo' else 'Inativo' end as SITUACAO,
    ATIVO,
    EMAIL AS EMAIL,
    FANTASIA AS FANTASIA,
    OBSERVACOES,
    DATANASC_CLIENTE AS NASCIMENTO,
    NUMERO,
    COMPLEMENTO,
    LIMITE_CREDITO,
    DATA_NASCIMENTO AS NASCIMENTO,
    INSCRICAO_MUNICIPAL AS IM,
    PROFISSAO AS PROFISSAO,
    ESTADO_CIVIL AS EST_CIVIL,
    SEXO,
    NOME_DO_PAI AS PAI,
    NOME_DA_MAE AS MAE
FROM CLIENTES
WHERE CLIENTE = 'S' AND COD_CLIENTE <> '0'
