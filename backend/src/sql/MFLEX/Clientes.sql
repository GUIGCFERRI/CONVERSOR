SELECT
    ID_CLIENTE AS CODIGO,
    CLIENTE AS NOME,
    APELIDO AS FANTASIA,
    CGC_CPF AS CNPJ_CNPF,
    RG_IE AS IE_RG,
    ENDERECO AS ENDERECO,
    PONTO_REF AS COMPLEMENTO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    UF AS UF,
    replace (replace(CEP,'-',''),'.','') AS CEP,
    TELEFONE AS TELEFONE,
    CELULAR AS CELULAR,
    TELEFAX AS FAX,
    EMAIL AS EMAIL,
    PAI AS PAI,
    MAE AS MAE,
    ESTADO_CIVIL AS EST_CIVIL,
    CONJUGE AS CONJUGE,
    NASC AS NASCIMENTO,
    LIMITE AS LIMITE_CREDITO,
    SEXO AS SEXO,
    CADASTRO AS CADASTRO,
    ULTIMA_COMPRA AS ULTIMA_VENDA
FROM CLIENTE