SELECT
  CLIENTES.CLIENTE AS CODIGO,
  CLIENTES.NOME AS NOME,
  CLIENTES.RAZAO AS FANTASIA,
  CLIENTES.ENDERECO AS ENDERECO,
  CLIENTES.BAIRRO AS BAIRRO,
  IBGE.NOME AS CIDADE,
  CLIENTES.CEP AS CEP,
  CLIENTES.ESTADO AS UF,
  case when CLIENTES.CPF = '' or CLIENTES.CPF is null then CLIENTES.CNPJ else CLIENTES.CPF END as CNPJ_CNPF,
  case when CLIENTES.RG = '' or CLIENTES.RG is null then CLIENTES.INSC_ESTADUAL else CLIENTES.RG END as IE_RG,
  CLIENTES.COMPLEMENTO AS COMPLEMENTO,
  CLIENTES.PAI AS PAI,
  CLIENTES.MAE AS MAE,
  CLIENTES.OBS AS OBSERVACOES,
  CLIENTES.TELEFONE AS TELEFONE,
  CLIENTES.FAX AS FAX,
  CLIENTES.CELULAR AS CELULAR,
  CLIENTES.E_MAIL AS EMAIL,
  CLIENTES.CONTATO AS CONTATO,
  CLIENTES.SEXO AS SEXO,
  CLIENTES.DATA_CADASTRO AS CADASTRO,
  CLIENTES.CREDITO AS LIMITE_CREDITO,
  CLIENTES.INSC_MUNICIPAL AS IM
FROM CLIENTES
LEFT JOIN IBGE ON IBGE.CODMUNIC = CLIENTES.CIDADE
