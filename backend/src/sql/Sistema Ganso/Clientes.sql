SELECT
  CLIENTE.CODIGO AS CODIGO,
  CLIENTE.NOME AS NOME,
  CLIENTE.RAZAO_SOCIAL AS FANTASIA,
  CLIENTE.ENDERECO AS ENDERECO,
  CLIENTE.NUMERO AS NUMERO,
  CLIENTE.COMPLEMENTO AS COMPLEMENTO,
  CLIENTE.BAIRRO AS BAIRRO,
  CIDADE_VILA.NOME AS CIDADE,
  CLIENTE.UF AS UF,
  CLIENTE.CEP AS CEP,
  CLIENTE.DATA_CADASTRO AS CADASTRO,
  CLIENTE.OBSERVACAO AS OBSERVACOES,
  CLIENTE.TELEFONE AS TELEFONE,
  CLIENTE.FAX AS FAX,
  CLIENTE.CELULAR AS CELULAR,
  CLIENTE.EMAIL AS EMAIL,
  CLIENTE.SEXO AS SEXO,
  CLIENTE.PESSOA AS CONTATO,
  case when CLIENTE.CPF = '' or CLIENTE.CPF is null then CLIENTE.CNPJ else CLIENTE.CPF END as CNPJ_CNPF,
  case when CLIENTE.RG = '' or CLIENTE.RG is null then CLIENTE.INSC_ESTADUAL else CLIENTE.RG END as IE_RG,
  CLIENTE.INSC_MUNICIPAL AS IM,
  CLIENTE.NASCIMENTO AS NASCIMENTO,
  CLIENTE.PROFISSAO AS PROFISSAO,
  CLIENTE.LIMITE AS LIMITE_CREDITO,
  CLIENTE.PAI AS PAI,
  CLIENTE.MAE AS MAE,
  CLIENTE.ESTADO_CIVIL AS EST_CIVIL,
  CLIENTE.CONJUGE AS CONJUGE,
  CLIENTE.NATURALIDADE AS NATURALIDADE
FROM CLIENTE
LEFT JOIN CIDADE_VILA ON CIDADE_VILA.CODIGO = CLIENTE.COD_CIDADE
