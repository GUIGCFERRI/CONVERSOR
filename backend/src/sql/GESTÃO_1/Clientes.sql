SELECT
  CLIENTE.CODIGO AS CODIGO,
  CLIENTE.RAZAO AS NOME,
  CLIENTE.FANTASIA AS FANTASIA,
  case when CLIENTE.CGC = '' or CLIENTE.CGC is null then CLIENTE.CPF else CLIENTE.CGC END as CNPJ_CNPF,
  case when CLIENTE.RG = '' or CLIENTE.RG is null then CLIENTE.INSCESTAD else CLIENTE.RG END as IE_RG,
  CLIENTE.TIPOEND ||' '|| CLIENTE.ENDERECO AS ENDERECO,
  CLIENTE.NUMEND AS NUMERO,
  CIDADE.DESCRICAO AS CIDADE,
  CLIENTE.ESTADO AS UF,
  CLIENTE.BAIRRO AS BAIRRO,
  CLIENTE.COMPLEMENTO AS COMPLEMENTO,
  CLIENTE.EMAIL AS EMAIL,
  CLIENTE.CEP AS CEP,
  CLIENTE.CONTATO AS CONTATO,
  CLIENTE.OBSERVACAO AS OBSERVACOES,
  CLIENTE.NASCIMENTO AS NASCIMENTO,
  CLIENTE.SEXO AS SEXO,
  CLIENTE.ESTCIVIL AS EST_CIVIL,
  CLIENTE.INSCMUN AS IM,
  CLIENTE.DATACADASTRO AS CADASTRO,
  CLIENTE.CONJUGE AS CONJUGE,
  CLIENTE.LIMITE_CREDITO AS LIMITE_CREDITO,
  CLIENTE.NOME_PAI AS PAI,
  CLIENTE.NOME_MAE AS MAE
FROM CLIENTE
LEFT JOIN CIDADE ON CIDADE.CODIGO = CLIENTE.CIDADE
