SELECT
    CLIENTE.ID AS CODIGO,
    CLIENTE.RAZAOSOCIAL AS NOME,
    CLIENTE.FANTASIA AS FANTASIA,
    CLIENTE.CONTATO AS CONTATO,
    CLIENTE.CNPJ AS CNPJ_CNPF,
    CLIENTE.INSCRICAO AS IE_RG,
    CLIENTE.IM AS IM,
    CLIENTE.TIPORUA ||' '|| CLIENTE.NOMERUA AS ENDERECO,
    CLIENTE.COMPLEMENTO AS COMPLEMENTO,
    CLIENTE.NUMERO AS NUMERO,
    CLIENTE.BAIRRO AS BAIRRO,
    CLIENTE.CIDADE AS CIDADE,
    CLIENTE.ESTADO AS UF,
    CLIENTE.CEP AS CEP,
    CLIENTE.DDD ||' ' || CLIENTE.FONE AS TELEFONE,
    CLIENTE.DDD2 ||' '|| CLIENTE.FONE2 AS CELULAR,
    CLIENTE.DDD ||' ' || CLIENTE.FAX AS FAX,
    CLIENTE.EMAIL AS EMAIL,
    CLIENTE.OBSERVACAO AS OBSERVACOES,
    CLIENTE.LIMITE AS LIMITE_CREDITO,
    CLIENTE.PAI AS PAI,
    CLIENTE.MAE AS MAE,
    CLIENTE.CONJUGE AS CONJUGE,
    CLIENTE.NASCIMENTO AS NASCIMENTO,
    CLIENTE.DATA_INC AS CADASTRO
FROM CLIENTE
WHERE CLIENTE.RAZAOSOCIAL <> '';
