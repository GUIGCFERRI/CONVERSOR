SELECT
    TCLI.CODCLI AS CODIGO,
    TCLI.NOME AS NOME,
    TCLI.APELIDIO AS FANTASIA,
    TCLI.ENDERECO AS ENDERECO,
    TCLI.COMPL AS COMPLEMENTO,
    TCLI.CEP AS CEP,
    TCLI.BAIRRO AS BAIRRO,
    TCLI.ESTADO AS UF,
    TCLI.FONE AS TELEFONE,
    TCLI.FAX AS FAX,
    TCLI.CELULAR AS CELULAR,
    TCLI.EMAIL AS EMAIL,
    case when TCLI.CPF = '' or TCLI.CPF is null then TCLI.CGC else TCLI.CPF END AS CNPJ_CNPF,
    case when TCLI.RG = '' or TCLI.RG is null then TCLI.IDINSC else TCLI.RG END AS IE_RG,
    CAST (TCLI.DTCAD AS DATE) AS CADASTRO,
    TCLI.LIMCREDITO AS LIMITE_CREDITO,
    TCLI.OBS AS OBSERVECOES,
    TCLI.CONTATO AS CONTATO,
    CAST (TCLI.DTNASCIMENTO AS DATE) AS NASCIMENTO,
    TCLI.ESTADOCIVIL AS EST_CIVIL,
    TCLI.NACIONALIDADE AS NACIONALIDADE,
    TCLI.PROFISSAO AS PROFISSAO
FROM TCLI 
WHERE TCLI.CODCLI <> '0'
