SELECT
	P.IDCODIGO_PRODUTO AS CODIGO,
	P.DESCRICAO_PRODUTO AS DESCRICAO,
	P.UNIDBASE_PRODUTO AS UND,
	P.PESO_PRODUTO AS PESO,
	P.MERCOSUL_PRODUTO AS COD_NCM,
	P.CODIGO_FABRICA_PRODUTO AS COD_FABRICANTE,
	P.CODIGO_BARRA_PRODUTO AS BARRAS,
	P.SALDO1_PRODUTO AS QTD,
	P.ESTMIN_PRODUTO AS QTD_IDEAL,
	P.COM_AVISTA_PRODUTO AS COMISSAO,
	P.VLR_UNIT_PRODUTO AS PRECO_VENDA,
	P.VLR_ATACADO_PRODUTO AS PRECO_ATACADO,
	P.OBSERVACAO_PRODUTO AS OBSERVACOES,
	P.TIPO_PRODUTO AS TIPO_ITEM,
	P.ALIQICMS_PRODUTO AS ELO,
	P.CUSTOMEDICO_PRODUTO AS CUSTO_MEDIO,
	P.CUSTOCOMPRA_PRODUTO AS PRECO_CUSTO,
	P.ULTIMADCOMP_PRODUTO AS ULTIMA_COMPRA,
	P.CADASTRO_PRODUTO AS DATA_CADASTRO,
	P.DIAS_PRODUTO AS VALIDADE_DIAS,
	P.LUCRO_PRODUTO AS MARGEM_LUCRO,
	P.ULTIMAVENDA_PRODUTO AS ULTIMA_VENDA,
	P.COR_PRODUTO AS COR,
	P.TAMANHO_PRODUTO AS TAMANHO,
	P.CSTSIMPLES_PRODUTO AS ST,
--	P.CSTDC_PRODUTO AS ST,	
--	LEFT (P.CFOPDENTRO_PRODUTO,3) AS ELO,
	P.CODIGOCEST_PRODUTO AS PERSONAL6,
	P.ALIQ_PIS_PRODUTO AS PIS_ALIQ_NOR,
	P.ALIQ_CONFIS_PRODUTO AS COFINS_ALIQ_NOR,
	G.DESCRICAO_GRUPO AS GRUPO,
	C.RAZAO_CLIENTE AS FORNECEDOR
FROM
	PRODUTO P
LEFT JOIN PRODUTOS_GRUPO G ON G.IDCODIGO_GRUPO = P.IDGRUPO_PRODUTO	
LEFT JOIN CLIENTES C ON C.IDCODIGO_CLIENTE = P.IDFORNECEDOR1_PRODUTO