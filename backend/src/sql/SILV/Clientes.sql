SELECT
    CADCLI.NOME AS NOME,
    CADCLI.FANTASIA AS FANTASIA,
    CADCLI.CONTATO AS CONTATO, 
    case when CADCLI.CPF = '' or CADCLI.CPF is null then CADCLI.CGC else CADCLI.CPF END AS CNPJ_CNPF,
    case when CADCLI.INSC = '' or CADCLI.INSC is null then CADCLI.CARTIND else CADCLI.INSC END AS IE_RG,  
    CADCLI.INSCRICAO_MUNICIPAL AS IM,
    CADCLI.E_MAIL AS EMAIL,
    CADCLI.ENDERES AS ENDERECO,
    CADCLI.BAIRRO AS BAIRRO,
    CADCLI.CEP AS CEP,
    CADCLI.CIDADE AS CIDADE,
    CADCLI.UF AS UF,
    CADCLI.FONE1 AS TELEFONE,
    CADCLI.FONE2 AS CELULAR,
    CADCLI.FAX AS FAX,
    CADCLI.PAI AS PAI,
    CADCLI.MAE AS MAE,
    CADCLI.SEXO AS SEXO,
    CADCLI.OBS AS OBSERVACOES,
    CADCLI.DATCAD AS CADASTRO,
    CADCLI.DATANASC AS NASCIMENTO,
    ESTCIVIL AS EST_CIVIL,
    CADCLI.CONJ AS CONJUGE
FROM CADCLI
WHERE CADCLI.loja = '45' OR CADCLI.loja = '02'
