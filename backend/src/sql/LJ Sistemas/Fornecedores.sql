SELECT CP003.FCONTADOR AS CODIGO,
CP003.NOME,
CP003.APELIDO AS FANTASIA,
CP003.CGC AS CNPJ_CNPF,
CP003.INSCESTADUAL AS IE_RG,
CP003.CONTATO,
CP003.ENDERECO,
CP003.BAIRRO,
REP004.DESCCID AS CIDADE,
CP003.UF,
CP003.CEP,
CP003.FONE AS TELEFONE,
CP003.FAX,
CP003.EMAIL,
CP003.NOMEREP AS CONTATO,
CP003.CELULAR
FROM CP003
LEFT JOIN REP004 ON CP003.CIDADE=REP004.CIDCONTADOR
where cp003.empresa=1 and REP004.empresa=1
