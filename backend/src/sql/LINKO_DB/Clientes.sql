SELECT
  ACCLIENTE.CODCLIENTE AS CODIGO,
  ACCLIENTE.CODESTADO AS UF,
  ACCLIENTE.TELEFONE AS TELEFONE,
  ACCLIENTE.FAX AS FAX,
  ACCLIENTE.CELULAR AS CELULAR,
  ACCLIENTE.CEP AS CEP,
  case when ACCLIENTE.CNPJ = '' or ACCLIENTE.CNPJ is null then ACCLIENTE.CNPF else ACCLIENTE.CNPJ END as CNPJ_CNPF,
  ACCLIENTE.INSCRICAO AS IE_RG,
  ACCLIENTE.DATACADASTRO AS CADASTRO,
  ACCLIENTE.OBSERVACOES AS OBSERVACOES,
  ACCLIENTE.ENDERECO AS ENDERECO,
  ACCLIENTE.NOME AS NOME,
  ACCLIENTE.EMAIL AS EMAIL,
  ACCLIENTE.NOME_FANTASIA AS FANTASIA,
  ACCLIENTE.NUMERO AS NUMERO,
  ACCLIENTE.CIDADE AS CIDADE,
  ACCLIENTE.COMPLEMENTO AS COMPLEMENTO,
  ACCLIENTE.BAIRRO AS BAIRRO,
  ACCLIENTE.INSCRICAOMUNICIPAL AS IM
FROM ACCLIENTE
