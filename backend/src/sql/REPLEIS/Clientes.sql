SELECT
  CADASTROCLIENTE.CODIGO AS CODIGO,
  CADASTROCLIENTE.NOMECLIENTE AS NOME,
  CADASTROCLIENTE.ENDERECO AS ENDERECO,
  CADASTROCLIENTE.NUMERO AS NUMERO,
  CADASTROCLIENTE.BAIRRO AS BAIRRO,
  CADASTROCLIENTE.CIDADE AS CIDADE,
  CADASTROCLIENTE.CEP AS CEP,
  CADASTROCLIENTE.ESTADO AS UF,
  CADASTROCLIENTE.EMAIL AS EMAIL,
  CADASTROCLIENTE.DATANASCI AS NASCIMENTO,
  CADASTROCLIENTE.DATACAD AS CADASTRO,
  CADASTROCLIENTE.TELEFONERES AS TELEFONE,
  CADASTROCLIENTE.FAX AS FAX,
  CADASTROCLIENTE.CELULAR AS CELULAR,
  case when CADASTROCLIENTE.CPF ='' then CADASTROCLIENTE.CNPJ else CADASTROCLIENTE.CPF end as CNPJ_CNPF,
  case when CADASTROCLIENTE.RG ='' then CADASTROCLIENTE.INSCESTADUAL else CADASTROCLIENTE.RG end as IE_RG,
  CADASTROCLIENTE.INSCMUNICIPAL AS IM,
  CADASTROCLIENTE.PAI AS PAI,
  CADASTROCLIENTE.MAE AS MAE,
  CADASTROCLIENTE.CREDITOMAX AS LIMITE_CREDITO,
  CADASTROCLIENTE.DEVEDOR AS VALOR_A_RECEBER,
  CADASTROCLIENTE.APELIDO AS CONTATO,
  CADASTROCLIENTE.OBS AS OBSERVACOES
FROM CADASTROCLIENTE
