SELECT FORNECEDOR.ID_FORNECEDOR AS CODIGO,
FORNECEDOR.FANTASIA AS NOME,
FORNECEDOR.RAZ_SOCIAL AS FANTASIA,
FORNECEDOR.CONTATO,
FORNECEDOR.MUNICIPIO AS CIDADE,
FORNECEDOR.CEP,
FORNECEDOR.UF,
FORNECEDOR.FONE AS TELEFONE,
FORNECEDOR.FAX,
FORNECEDOR.CELULAR,
FORNECEDOR.CNPJ AS CNPJ_CNPF,
FORNECEDOR.IE AS IE_RG,
FORNECEDOR.ENDERECO,
FORNECEDOR.BAIRRO,
FORNECEDOR.COMPLEMENTO,
FORNECEDOR.NUMERO,
FORNECEDOR.EMAIL,
FORNECEDOR.OBS AS OBSERVACOES
FROM FORNECEDOR
WHERE FORNECEDOR.ID_FORNECEDOR>0
