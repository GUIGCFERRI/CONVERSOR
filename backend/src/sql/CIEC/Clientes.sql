SELECT
    CLI_CODIGO AS CODIGO,
    CLI_RAZAO AS NOME,
    CLI_FANTA AS FANTASIA,
    replace (replace (replace(CLI_CNPJ,'/',''),'-',''),'.','')  AS CNPJ_CNPF,
    CLI_IERG AS IE_RG,
    CLI_ENDERECO AS ENDERECO,
    CLI_NUMERO AS NUMERO,
    CLI_COMPLEM AS COMPLEMENTO,
    CLI_BAIRRO AS BAIRRO,
    CLI_CIDADE AS CIDADE,
    replace (replace(CLI_CEP,'-',''),'.','') AS CEP,
    CLI_ESTADO AS UF,
    CLI_EMAIL AS EMAIL,
    replace (replace(CLI_FONES,'/',''),'-','') AS TELEFONE,
    CLI_FAX AS FAX,
    CLI_CELULAR AS CELULAR,
    CLI_OBSERVACAO AS OBSERVACOES, 
    CLI_PAI AS PAI,
    CLI_MAE AS MAE,
    CLI_ESTCIVIL AS EST_CIVIL,
    CLI_CONJUGE AS CONJUGE,
    CLI_LIMITE AS LIMITE_CREDITO,
    CLI_NACISM AS NASCIMENTO,
    CLI_CADASTRO AS CADASTRO
FROM CLIENTE
WHERE CLI_CADASTRO IS NOT NULL