SELECT
    CDCLIE AS CODIGO,
    NOME AS NOME,
    FANTASIA AS FANTASIA,
    ENDRES AS ENDERECO,
    NUMERO AS NUMERO,
    COMPLEMENTO AS COMPLEMENTO,
    CIDRES AS CIDADE,
    BAIRES AS BAIRRO,
    FONERES AS TELEFONE,
    CEPRES AS CEP,
    ESTRES AS UF,
    FAXRES AS FAX,
    CELRES AS CELULAR,
    EMAIL AS EMAIL,
    case when CPF = '' or CPF is null then CGC else CPF END AS CNPJ_CNPF,
    case when IDENTIDADE = '' or IDENTIDADE is null then INSCEST else IDENTIDADE END AS IE_RG,
    INSCMUN AS IM,  
    NASCIMENTO AS NASCIMENTO,
    CADASTRO AS CADASTRO,
    LIMCRED AS LIMITE_CREDITO,
    OBS AS OBSERVACOES,
    SEXO AS SEXO
FROM CLIENTES
