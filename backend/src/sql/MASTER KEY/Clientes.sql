SELECT
  CLIENTES.CL_CODIGO AS CODIGO,
  CLIENTES.CL_CODIGO AS PERSONAL4,
  CLIENTES.CL_NOME AS NOME,
  CLIENTES.CL_ENDERECO AS ENDERECO,
  CLIENTES.CL_BAIRRO AS BAIRRO,
  CLIENTES.CL_CIDADE AS CIDADE,
  CLIENTES.CL_UF AS UF,
  CLIENTES.CL_CEP AS CEP,
  CLIENTES.CL_FONE AS TELEFONE,
  CLIENTES.CL_FAX AS FAX,
  CLIENTES.CL_CONTATO AS CONTATO,
  case when CLIENTES.CL_CGC = '' or CLIENTES.CL_CGC is null then CLIENTES.CL_CPF else CLIENTES.CL_CGC END as CNPJ_CNPF,
  case when CLIENTES.CL_INSCRICAO = '' or CLIENTES.CL_INSCRICAO is null then CLIENTES.CL_CI else CLIENTES.CL_INSCRICAO END as IE_RG,
  CLIENTES.CL_DTNASC AS NASCIMENTO,
  CLIENTES.CL_ESTCIVIL AS EST_CIVIL,
  CLIENTES.CL_CONJUGE AS CONJUGE,
  CLIENTES.CL_OBSGERAL AS OBSERVACOES,
  CLIENTES.CL_EMAIL AS EMAIL,
  CLIENTES.CL_LIMITECRED AS LIMITE_CREDITO,
  CLIENTES.CL_NOMFAN AS FANTASIA,
  CLIENTES.CL_SEXO AS SEXO,
  CLIENTES.CL_NRO AS NUMERO,
  CLIENTES.CL_COMPL AS COMPLEMENTO,
  CLIENTES.CL_CELULAR AS CELULAR
FROM CLIENTES
WHERE CLIENTES.CL_TIPO <> 'F' 
