, P.PRO_MARGEM_LUCRO as MARGEM_LUCRO
, P.PRO_PRECO_VENDA as PRECO_VENDA
, P.PRO_MINIMO_PRECO_VENDA as ATACADO
, P.PRO_OBS as OBSERVACOES
, LEFT (P.PRO_UNI,3) as UND
