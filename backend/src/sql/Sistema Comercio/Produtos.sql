, P.PRO_REFERENCIA as PERSONAL1
, P.PRO_LOTE as PERSONAL2
, P.PRO_TAMANHO as TAMANHO
, P.PRO_COR as COR
, P.PRO_COMIS_VENDEDOR_PORC as COMISSAO
, P.PRO_IPI_PORCENTAGEM as ALIQ_IPI
, P.PRO_ALIQ_IPI as ALIQ_IPI_VENDA
