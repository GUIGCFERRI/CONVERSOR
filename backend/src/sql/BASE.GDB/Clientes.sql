SELECT
  CAD_CLIENTES.COD AS CODIGO,
  CAD_CLIENTES.NOME AS NOME,
  CAD_CLIENTES.CPF AS CNPJ_CNPF,
  CAD_CLIENTES.RG AS IE_RG,
  CAD_CLIENTES.ENDE AS ENDERECO,
  CAD_CLIENTES.BAIRRO AS BAIRRO,
  CAD_CLIENTES.CIDADE AS CIDADE,
  CAD_CLIENTES.UF AS UF,
  CAD_CLIENTES.CEP AS CEP,
  CAD_CLIENTES.FONE1 AS TELEFONE,
  CAD_CLIENTES.FONE2 AS FAX,
  CAD_CLIENTES.IMAIL AS EMAIL,
  CAD_CLIENTES.OBS AS OBSERVACOES,
  CAD_CLIENTES.CELULAR AS CELULAR,
  CAD_CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO,
  CAD_CLIENTES.CONJUGUE AS CONJUGE,
  CAD_CLIENTES.DATA_NASC AS NASCIMENTO,
  CAD_CLIENTES.PROFISSAO AS PROFISSAO,
  CAD_CLIENTES.NOME_PAI AS PAI,
  CAD_CLIENTES.NOME_MAE AS MAE,
  CAD_CLIENTES.VALOR_DEBITO AS VALOR_A_RECEBER,
CAD_CLIENTES.VALOR_DEBITO AS PERSONAL1
FROM CAD_CLIENTES
WHERE CAD_CLIENTES.NOME IS NOT NULL
