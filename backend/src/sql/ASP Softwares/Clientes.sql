SELECT
  CLIENTES.IDCLIENTE AS CODIGO,
  CLIENTES.RAZAOSOCIAL AS NOME,
  CLIENTES.NOME AS FANTASIA,
  CLIENTES.CONTATO AS CONTATO,
  CLIENTES.DATANASC AS NASCIMENTO,
  case when CLIENTES.CPF = '' or CLIENTES.CPF is null then CLIENTES.CNPJ else CLIENTES.CPF END as CNPJ_CNPF,
  case when CLIENTES.IDENTIDADE = '' or CLIENTES.IDENTIDADE is null then CLIENTES.INSCEST else CLIENTES.IDENTIDADE END as IE_RG,
  CLIENTES.INSCMUNICIPAL AS IM,
  CLIENTES.ENDERECO AS ENDERECO,
  CLIENTES.NUMERO AS NUMERO,
  CLIENTES.CEP AS CEP,
  CLIENTES.COMPLEMENTO AS COMPLEMENTO,
  CLIENTES.BAIRRO AS BAIRRO,
  CLIENTES.CIDADE AS CIDADE,
  CLIENTES.UF AS UF,
  CLIENTES.FONE AS TELEFONE,
  CLIENTES.CELULAR AS CELULAR,
  CLIENTES.FAX AS FAX,
  CLIENTES.OBS AS OBSERVACOES,
  CLIENTES.EMAIL AS EMAIL,
  CLIENTES.PAI AS PAI,
  CLIENTES.MAE AS MAE,
  CLIENTES.CONJUGE AS CONJUGE,
  CLIENTES.ESTADOCIVIL AS EST_CIVIL,
  CLIENTES.PROFISSAO AS PROFISSAO
FROM CLIENTES
WHERE CLIENTES.RAZAOSOCIAL IS NOT NULL
