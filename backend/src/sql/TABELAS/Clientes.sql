SELECT
    CLIENTES.CODIGO AS CODIGO,
    CLIENTES.NOME AS NOME,
    CLIENTES.FANTASIA AS FANTASIA,
    case when CLIENTES.CPF = '' or CLIENTES.CPF is null then CLIENTES.CPF_CNPJ else CPF END AS CNPJ_CNPF,
    case when CLIENTES.RG = '' or CLIENTES.RG is null then CLIENTES.RG_IE else RG END AS IE_RG,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.NUMERO AS NUMERO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.UF AS UF,
    CLIENTES.CEP AS CEP,
    CLIENTES.FONE AS TELEFONE,
    CLIENTES.FAX AS FAX,
    CLIENTES.CEL AS CELULAR,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.SEXO AS SEXO,
    CLIENTES.PAI AS PAI,
    CLIENTES.MAE AS MAE,
    CLIENTES.CONJUGE AS CONJUGE,
    CLIENTES.ESTCIVIL AS EST_CIVIL,
    CLIENTES.LIMITECREDITO AS LIMITE_CREDITO,
    CLIENTES.OBS AS OBSERVACOES,
    CLIENTES.DATACAD AS CADASTRO
FROM CLIENTES