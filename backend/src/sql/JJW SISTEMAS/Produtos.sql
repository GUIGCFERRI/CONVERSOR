SELECT
	P.COD_PRODUTO AS CODIGO,
	P.NUM_PRODUTO AS COD_FABRICANTE,
--	P.NUM_REFER_FABRICANTE_PRODUTO AS COD_FABRICANTE,	
	P.SIG_UNIDADE AS UND,
	P.COD_ORIFIS AS OST,
	P.COD_TRIICM AS ST,
	P.DES_PRODUTO AS DESCRICAO,
	P.PES_PRODUTO AS PESO,
	P.QTD_EMBALAGEM_PRODUTO AS FAT_CONV,
	P.PER_LUCRO_PRODUTO AS MARGEM_LUCRO,
	P.PER_COMISSAO_PRODUTO AS COMISSAO,
	P.NUM_DIAS_VALIDADE_PRODUTO AS VALIDADE_DIAS,
--	P.QTD_ESTOQUE_MIN_PRODUTO AS QTD_IDEAL,
--	P.VAL_PROMOCAO_PRODUTO AS PRECO_ATACADO,
--	P.QTD_PROMOCAO_PRODUTO AS UTILIZAR_PRECO_ATACADO,
--	P.DES_APLICACAO_PRODUTO AS OBSERVACOES,	
	P.OBS_PRODUTO AS OBSERVACOES,
	CAST (P.DTH_INCLUIU_PRODUTO AS DATE) AS DATA_CADASTRO,
	P.PER_PIS_PRODUTO AS PIS_ALIQ_NOR,
	P.COD_ST_PIS_PRODUTO AS PIS_CODIGO,
	P.COD_ST_PIS_ENT_PRODUTO AS PISE_CODIGO,
	P.PER_COFINS_PRODUTO AS COFINS_ALIQ_NOR,
	P.COD_ST_COFINS_PRODUTO AS COFINS_CODIGO,
	P.COD_ST_COFINS_ENT_PRODUTO AS COFINSE_CODIGO,
	P.COD_ANP_PRODUTO AS COD_ANP,
	P.COD_CEST_PRODUTO AS PERSONAL6,
	CF.NUM_CLAFIS AS COD_NCM,
	PE.VAL_CUSTO_PROEST AS PRECO_CUSTO,
	PE.VAL_CUSTO_MEDIO_PROEST AS CUSTO_MEDIO,
	PE.QTD_ESTOQUE_PROEST AS QTD,
	PE.QTD_ESTOQUE_MIN_PRODUTO AS QTD_IDEAL,
	GP.DES_GRUPRO AS GRUPO,
	M.DES_MARCA AS CARACTERISTICAS,
	F.DES_FAMILIA AS FAMILIA,
	LPI.VAL_LISPREITE AS PRECO_VENDA,
	MAX (PCA.NUM_BARRA_ALTERNATIVO) AS BARRAS
FROM
	PRODUTOS P
LEFT JOIN CLASSIFICACOES_FISCAIS CF ON CF.COD_CLAFIS = P.COD_CLAFIS 
LEFT JOIN PRODUTOS_ESTOQUES PE ON PE.COD_PROGRA = P.COD_PRODUTO
LEFT JOIN GRUPOS_PRODUTOS GP ON GP.COD_GRUPRO = P.COD_GRUPRO 
LEFT JOIN MARCAS M ON M.COD_MARCA = P.COD_MARCA 
LEFT JOIN FAMILIAS F ON F.COD_FAMILIA = P.COD_FAMILIA 
LEFT JOIN LISTAS_PRECOS_ITENS LPI ON LPI.COD_PRODUTO = P.COD_PRODUTO 
LEFT JOIN PRODUTOS_CODIGOS_ALTERNATIVOS PCA ON PCA.COD_PRODUTO = P.COD_PRODUTO 
GROUP BY
    P.COD_PRODUTO,
    P.NUM_PRODUTO,
--    P.NUM_REFER_FABRICANTE_PRODUTO,    
    P.SIG_UNIDADE,
    P.COD_ORIFIS,
    P.COD_TRIICM,
    P.DES_PRODUTO,
    P.PES_PRODUTO,
    P.QTD_EMBALAGEM_PRODUTO,
    P.PER_LUCRO_PRODUTO,
    P.PER_COMISSAO_PRODUTO,
    P.NUM_DIAS_VALIDADE_PRODUTO,
    P.OBS_PRODUTO,
    CAST (P.DTH_INCLUIU_PRODUTO AS DATE),
    P.PER_PIS_PRODUTO,
    P.COD_ST_PIS_PRODUTO,
    P.COD_ST_PIS_ENT_PRODUTO,
    P.PER_COFINS_PRODUTO,
    P.COD_ST_COFINS_PRODUTO,
    P.COD_ST_COFINS_ENT_PRODUTO,
    P.COD_ANP_PRODUTO,
    P.COD_CEST_PRODUTO,
    CF.NUM_CLAFIS,
    PE.VAL_CUSTO_PROEST,
    PE.VAL_CUSTO_MEDIO_PROEST,
    PE.QTD_ESTOQUE_PROEST,
    PE.QTD_ESTOQUE_MIN_PRODUTO,
    GP.DES_GRUPRO,
    M.DES_MARCA,
    F.DES_FAMILIA,
    LPI.VAL_LISPREITE;
