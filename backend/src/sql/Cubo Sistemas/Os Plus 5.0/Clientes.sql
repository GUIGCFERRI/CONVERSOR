SELECT
  CLIENTES.IDCLIENTE AS CODIGO,
  CLIENTES.RAZAO AS NOME,
  CLIENTES.FANTASIA AS FANTASIA,
  CLIENTES.CONTATO AS CONTATO,
  CLIENTES.FONE1 AS TELEFONE,
  CLIENTES.FONE2 AS CELULAR,
  CLIENTES.FONE3 AS FAX,
  CLIENTES.CNPJCPF AS CNPJ_CNPF,
  case when CLIENTES.IE = '' or CLIENTES.IE is null then CLIENTES.RG else CLIENTES.IE END as IE_RG,
  CLIENTES.IM AS IM,
  CLIENTES.EMAIL1 AS EMAIL,
  CLIENTES.ENDERECO AS ENDERECO,
  CLIENTES.NUMERO AS NUMERO,
  CLIENTES.BAIRRO AS BAIRRO,
  CLIENTES.CIDADE AS CIDADE,
  CLIENTES.UF AS UF,
  CLIENTES.CEP AS CEP,
  CLIENTES.COMPLEMENTO AS COMPLEMENTO,
  CLIENTES.DATANASC AS NASCIMENTO,
  CLIENTES.LIMITECRED AS LIMITE_CREDITO,
  CLIENTES.DATACAD AS CADASTRO
FROM CLIENTES