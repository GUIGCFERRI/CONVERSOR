SELECT
	MC03PRO.AC03CODI AS PERSONAL1,
	MC03PRO.AC03DESC AS DESCRICAO,
	MC03PRO.AC03REF AS COD_FABRICANTE,
	MC03PRO.AN03SLAM AS QTD,
	MC03PRO.AC03CBR AS BARRAS,
	MC03PRO.AC03UN AS UND,
	MC03PRO.AD03DTCD AS DATA_CADASTRO,
	MC03PRO.AN03PRC1 AS PRECO_VENDA,
	MC03PRO.AN03CTCO AS PRECO_CUSTO,
	MC03PRO.AN03CTME AS CUSTO_MEDIO,
	MC03PRO.AN03_CUSTO_COMPRA AS PRECO_CUSTO,
	MC03PRO.AN03MARG AS MARGEM_LUCRO,
	MC03PRO.AC03_CODIGO_DCB AS COD_NCM,
	MC03PRO.AC03_CEST AS PERSONAL6,	
	MC12GRUPO.AC12DESC AS GRUPO,
	MC31SUBGRUPO.AC31DESC AS FAMILIA,
	MC40TBICM.AC40_CSOSN AS ST,
	MC40TBICM.AC40_CFOP_RS AS CF,
	LEFT(REPLACE (MC40TBICM.AC40_CFOP_RS,'.',''),3) AS ELO
FROM MC03PRO
LEFT JOIN MC12GRUPO ON MC03PRO.AC03GRUPO = MC12GRUPO.AC12CODIGO
LEFT JOIN MC40TBICM ON MC40TBICM.AC40CODIGO = MC03PRO.AC03TBICM
LEFT JOIN MC31SUBGRUPO ON MC31SUBGRUPO.AC31CODIGO = MC03PRO.AC03SBG