SELECT
    CODIGO_CLIENTE AS CODIGO,
    DESCRICAO AS NOME,
    RAZAO_SOCIAL AS FANTASIA,
    CONTATO AS CONTATO,
    CPF_CNPJ AS CNPJ_CNPF,
    case when RG= '' or RG is null then INSCRICAO_ESTADUAL else RG END AS IE_RG,
    ENDERECO AS ENDERECO,
    COMPLEMENTO AS COMPLEMENTO,
    NUMERO AS NUMERO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    ESTADO AS UF,
    CEP AS CEP,
    TELEFONE1 AS TELEFONE,
    FAX AS FAX,
    CELULAR AS CELULAR,
    EMAIL AS EMAIL,
    OBSERVACAO AS OBSERVACOES,
    LIMITE_CREDITO AS LIMITE_CREDITO,
    SEXO AS SEXO,
    PROFISSAO AS PROFISSAO,
    NOME_MAE AS MAE,
    NOME_PAI AS PAI,
    NATURALIDADE AS NATURALIDADE,
    DATA_NASCIMENTO AS NASCIMENTO,
    DATA_CADASTRO AS CADASTRO
FROM CLIENTES