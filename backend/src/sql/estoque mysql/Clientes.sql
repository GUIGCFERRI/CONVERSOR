SELECT
	CODIGO,
	CADASTRADO_EM AS CADASTRO,
	NOME,
	ENDERECO,
	NUMERO,	
	DATA_NCTO AS NASCIMENTO,
	CIDADE,
	UF,
	BAIRRO,
	CEP,
	TELEFONE,
	TELEFONE2 AS PERSONAL1,
	FAX,
	CELULAR,
	E_MAIL AS EMAIL,
	SEXO,
	ESTADOCIVIL AS EST_CIVIL,
    REPLACE(REPLACE(REPLACE(REPLACE((CASE WHEN CPF = '' OR CPF IS NULL THEN CNPJ ELSE CPF END), '.', ''), '/', ''), '-', ''), ',', '') AS CNPJ_CNPF,
    REPLACE(REPLACE(REPLACE(REPLACE(I_ESTADUAL, '.', ''), '/', ''), '-', ''), ',', '') AS IE_RG,
	OBS AS OBSERVACOES,
	CONJ_NOME AS CONJUGE,
	PAI_NOME AS PAI,
	MAE_NOME AS MAE,
	LOCAL_TRABALHO AS LOCTRA,
	LIMITECREDITO AS LIMITE_CREDITO
FROM
	CLIENTES