SELECT
    CAST (CLIENTE AS INTEGER) AS CODIGO,
    NOME AS NOME,
    NOMEFANTASIA AS FANTASIA,
    CIC_CGC AS CNPJ_CNPF,
    RG_INS AS IE_RG,
    INSCRICAO_MUNICIPAL AS IM,
    ENDERECO AS ENDERECO,
    NUMERO AS NUMERO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    UF AS UF,
    replace(FAR106.CEP,'-','') AS CEP,
    FONE AS TELEFONE,
    CELULAR AS CELULAR,
    FAX AS FAX,
    EMAIL AS EMAIL,
    CONJUGE AS CONJUGE,
    LIMITE AS LIMITE_CREDITO,
    OBSERVACAO AS OBSERVACOES,
    CAST (NASCIMENTO AS DATE) AS NASCIMENTO,
    CAST (ULT_COMPRA AS DATE) AS ULTIMA_COMPRA,
    CAST (DATA_INCLUSAO AS DATE) AS CADASTRO
FROM FAR106
