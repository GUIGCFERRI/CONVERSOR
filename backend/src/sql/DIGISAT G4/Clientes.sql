SELECT
   CLIENTES.CODIGO AS CODIGO,
   CLIENTES.NOME AS NOME,
   CLIENTES.FANTASIA AS FANTASIA,
   CLIENTES.CONTATO AS CONTATO,
   case when CLIENTES.CNPJ = '' or CLIENTES.CNPJ is null then CLIENTES.CPF else CLIENTES.CNPJ END AS CNPJ_CNPF,
   case when CLIENTES.IE = '' or CLIENTES.IE is null then CLIENTES.RG else CLIENTES.IE END AS IE_RG,
   CLI_ENDERECOS.ENDERECO AS ENDERECO,
   CLI_ENDERECOS.NUMERO AS NUMERO,
   CLI_ENDERECOS.COMPLEMENTO AS COMPLEMENTO,
   CLI_ENDERECOS.BAIRRO AS BAIRRO,
   CLI_ENDERECOS.CIDADE AS CIDADE,
   CLI_ENDERECOS.UF AS UF,
   CLI_ENDERECOS.CEP AS CEP,
   CLI_ENDERECOS.ENDERECO_COB AS COB_ENDERECO,
   CLI_ENDERECOS.NUMERO_COB AS COB_NUMERO,
   CLI_ENDERECOS.COMPLEMENTO_COB AS COB_COMPLEMENTO,
   CLI_ENDERECOS.BAIRRO_COB AS COB_BAIRRO,
   CLI_ENDERECOS.CIDADE_COB AS COB_CIDADE,
   CLI_ENDERECOS.UF_COB AS COB_UF,
   CLI_ENDERECOS.CEP_COB AS COB_CEP,
   CLIENTES.TELEFONE AS TELEFONE,
   CLIENTES.CELULAR AS CELULAR,
   CLIENTES.FAX AS FAX,
   substring(clientes.observacoes from 1 for 1000) AS OBSERVACOES,
   CLI_ADICIONAIS.EMAIL AS EMAIL,
   COALESCE(CLIENTES.LIMITE_CREDITO, 0) AS LIMITE_CREDITO,
   CLIENTES.DIA_ACERTO AS DIA_DE_ACERTO,
   COALESCE(CLIENTES.A_RECEBER, 0) AS VALOR_A_RECEBER,
   COALESCE(CLIENTES.ATRASADO, 0) AS VALOR_EM_ATRASO,
   COALESCE(CLI_ADICIONAIS.DATA_CADASTRO, CURRENT_DATE) AS CADASTRO,
   COALESCE(CLI_ADICIONAIS.ULTIMA_VENDA, CURRENT_DATE) AS ULTIMA_VENDA,
   CLI_ADICIONAIS.PROFISSAO AS PROFISSAO,
   CLI_ADICIONAIS.CONJUGE AS CONJUGE,
   CLI_ADICIONAIS.NATURALIDADE AS NATURALIDADE,
   CLI_ADICIONAIS.nascimento AS nascimento,
   CLI_ADICIONAIS.nome_pai AS pai,
   CLI_ADICIONAIS.nome_mae AS mae
FROM CLIENTES
left JOIN CLI_ENDERECOS ON CLIENTES.CODIGO = CLI_ENDERECOS.CODIGO
left JOIN CLI_ADICIONAIS ON CLIENTES.CODIGO = CLI_ADICIONAIS.CODIGO
left join cli_personalizados on cli_personalizados.codigo = clientes.codigo
