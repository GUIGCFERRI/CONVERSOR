SELECT PRODUTO.ID AS CODIGO,
PRODUTO.GTIN AS BARRAS,
PRODUTO.CODIGO_INTERNO AS COD_FABRICANTE,
PRODUTO.REFERENCIA AS PERSONAL1,
PRODUTO.NOME AS DESCRICAO,
PRODUTO.VALOR_VENDA AS PRECO_VENDA,
PRODUTO.QTD_ESTOQUE AS QTD,
PRODUTO.ESTOQUE_MIN AS QTD_IDEAL,
PRODUTO.IPPT, 
PRODUTO.PERCENTUAL_LUCRO AS MARGEM_LUCRO,
PRODUTO.CST_A AS OST,
PRODUTO.DATA_ULTIMA_NF AS ULTIMA_VENDA,
PRODUTO.CST_PIS_CONFIS_SAIDA AS PIS_CODIGO,
PRODUTO.CST_PIS_CONFIS_SAIDA AS COFINS_CODIGO,
PRODUTO.CST_PIS_CONFIS_ENTRADA AS PISE_CODIGO,
PRODUTO.CST_PIS_CONFIS_ENTRADA AS COFINSE_CODIGO,
PRODUTO.CST_IPI AS IPI_CODIGO_VENDA,
PRODUTO.CEST AS PERSONAL6,
PRODUTO.QTD_ATACADO AS QTD_PRECO_ATACADO,
PRODUTO.FATOR_CONVERSAO AS FAT_CONV,
PRODUTO.NCM AS COD_NCM,
PRODUTO.DATA_ESTOQUE AS DATA_CADASTRO,
PRODUTO.CSOSN AS ST,
PRODUTO.CUSTO_MEDIO,
PRODUTO.PRECO_CUSTO,
GRUPO.DESCRICAO AS GRUPO,
UNIDADE_PRODUTO.NOME AS UND,
PESSOA.NOME AS FORNECEDOR
FROM PRODUTO
LEFT JOIN GRUPO ON PRODUTO.ID_GRUPO=GRUPO.ID
LEFT JOIN UNIDADE_PRODUTO ON PRODUTO.ID_UNIDADE_PRODUTO=UNIDADE_PRODUTO.ID
LEFT JOIN PESSOA ON PESSOA.ID = PRODUTO.ID_FORNECEDOR