SELECT
    --FAVORECIDOS.CODIGO AS CODIGO,
    FAVORECIDOS.NOME AS NOME,
    FAVORECIDOS.RAZAO AS FANTASIA,
    FAVORECIDOS.CONTATO AS CONTATO,
    FAVORECIDOS.CPF_CNPJ AS CNPJ_CNPF,
    case when FAVORECIDOS.INSCRICAO_EST = '' or FAVORECIDOS.INSCRICAO_EST is null then FAVORECIDOS.RG else FAVORECIDOS.INSCRICAO_EST END AS IE_RG,
    FAVORECIDOS.INSCRICAO_MUN AS IM, 
    FAVORECIDOS.ENDERECO AS ENDERECO,
    FAVORECIDOS.NRO AS NUMERO,
    FAVORECIDOS.BAIRRO AS BAIRRO,
    FAVORECIDOS.CIDADE AS CIDADE,
    FAVORECIDOS.UF AS UF,
    FAVORECIDOS.CEP AS CEP,
    FAVORECIDOS.FONE1 AS TELEFONE,
    FAVORECIDOS.FAX AS FAX,
    FAVORECIDOS.CELULAR AS CELULAR,
    FAVORECIDOS.EMAIL AS EMAIL,
    TIPOFAVORECIDO,
    FAVORECIDOS.LIMITECREDITO AS LIMITE_CREDITO,
    FAVORECIDOS.DATACADASTRO AS CADASTRO,
    FAVORECIDOS.OBS AS OBSERVACOES,  
    FAVORECIDOS.DATANASC AS NASCIMENTO,
    FAVORECIDOS.ULTCOMPRA AS ULTIMA_VENDA,
    FAVORECIDOS.SEXO AS SEXO
FROM FAVORECIDOS
WHERE FAVORECIDOS.TIPOFAVORECIDO <> '2' AND FAVORECIDOS.tipofavorecido <> '-1'  -- //CLIENTES
