SELECT CED001.CODIGO AS COD_FABRICANTE,
CED001.NOME AS DESCRICAO,
CED001.UNIDADE AS UND,
CED001.GRUPO,
CED001.VRVENDA AS PRECO_VENDA,
CED001.VRCUSTO AS PRECO_CUSTO,
CED001.OBS AS OBSERVACOES,
SUBSTRING(CED001.CT FROM 1 FOR 1) AS OST,
CASE WHEN SUBSTRING(CED001.CT FROM 4 FOR 4)='' THEN CED001.CT
ELSE SUBSTRING(CED001.CT FROM 2 FOR 4) END AS ST,
CED001.CF AS COD_NCM,
CED001.VR_ATACADO AS PRECO_ATACADO,
CED001.MARCA AS CARACTERISTICAS,
CED001.CEST AS PERSONAL6,
CED101.CBARRA AS BARRAS,
CED101.QTDE AS QTD
FROM CED001
LEFT JOIN CED101 ON CED001.CODIGO=CED101.CODIGO
