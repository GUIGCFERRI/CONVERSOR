SELECT
    CLIENTE_ID AS CODIGO,
    CLIENTE AS NOME,
    CLI_CPF AS CNPJ_CNPF,
    CLI_RG AS IE_RG,
    CLI_ENDERECO AS ENDERECO,
    CLI_BAIRRO AS BAIRRO,
    CLI_NUMERO AS NUMERO,
    CLI_COMPLEMENTO AS COMPLEMENTO,
    CLI_CIDADE AS CIDADE,
    CLI_CEP AS CEP,
    CLI_UF AS UF,
    DDD_TELEFONE ||'' || CLI_TELEFONE AS TELEFONE,
    DDD_CELULAR ||'' || CLI_CELULAR AS CELULAR,
    CLI_EMAIL AS EMAIL,
    CLI_ESTADO_CIVIL AS EST_CIVIL,
    CLI_SEXO AS SEXO,
    CLI_CONJUGE AS CONJUGE,
    CLI_PAI AS PAI,
    CLI_MAE AS MAE,
    CLI_LIMITE AS LIMITE_CREDITO,
    CLI_OBSERVACAO AS OBSERVACOES,
    coalesce(CAST (CLI_DATA_CADASTRO AS DATE), current_date) AS CADASTRO,
    CAST (CLI_NASCIMENTO AS DATE) AS NASCIMENTO
FROM CLIENTES
