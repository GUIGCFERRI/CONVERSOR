SELECT CLIENTES.CODCLI AS CODIGO,
       CLIENTES.CPFCGC AS CNPJ_CNPF,
       CLIENTES.INSEST AS IE_RG,
       CLIENTES.NOMCLI AS NOME,
       CLIENTES.FANCLI AS FANTASIA,
       CLIENTES.ENDERE AS ENDERECO,
       CLIENTES.BAIRRO AS BAIRRO,
       CIDADES.CIDADE AS CIDADE,
       CLIENTES.ESTADO AS UF,
       CLIENTES.CEPCLI AS CEP,
       CLIENTES.TELCLI AS TELEFONE,
       CLIENTES.CELCLI AS CELULAR,
       CLIENTES.DATNAS AS NASCIMENTO,
       CLIENTES.PROFIS AS PROFISSAO,
       CLIENTES.ESTCIV AS EST_CIVIL,
       CLIENTES.PAICLI AS PAI,
       CLIENTES.MAECLI AS MAE,
       CLIENTES.CONJUG AS CONJUGE,
       CLIENTES.LIMITE AS LIMITE_CREDITO,
       CLIENTES.HISNEG AS OBSERVACOES,
       CLIENTES.DATCAD AS CADASTRO
FROM CLIENTES
     LEFT JOIN CIDADES ON CIDADES.CODCID = CLIENTES.ID_CODCID
