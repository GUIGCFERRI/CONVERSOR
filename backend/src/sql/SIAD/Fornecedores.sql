SELECT
  FORNEC.CODIGO AS CODIGO,
  FORNEC.NOME AS NOME,
  FORNEC.ENDERECO AS ENDERECO,
  FORNEC.BAIRRO AS BAIRRO,
  FORNEC.CIDADE AS CIDADE,
  REPLACE (FORNEC.ESTADO, '', '-') AS UF,
  --FORNEC.ESTADO AS UF,
  FORNEC.CEP AS CEP,
  FORNEC.TELEFONE AS TELEFONE,
  FORNEC.CPF_CGC AS CNPJ_CNPF,
  FORNEC.TELEFONE1 AS TELEFONE,
  FORNEC.FAX AS FAX,
  FORNEC.INSCEST AS IE_RG,
  FORNEC.CONTATO AS CONTATO,
  FORNEC.OBS1 AS OBSERVACOES,
  FORNEC.EMAIL AS EMAIL,
  FORNEC.NUM_END AS NUMERO,
  FORNEC.COMPLEMENTO AS COMPLEMENTO,
  FORNEC.NOMEFANTASIA AS FANTASIA
FROM FORNEC
