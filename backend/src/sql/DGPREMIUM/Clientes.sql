SELECT
  CLIENTES.CODIGO AS CODIGO,
  CLIENTES.NOME AS NOME,
  CLIENTES.FANTASIA AS FANTASIA,
  CLIENTES.ENDERECO AS ENDERECO,
  CLIENTES.COMPLEMENTO AS COMPLEMENTO,
  CLIENTES.BAIRRO AS BAIRRO,
  CLIENTES.CIDADE AS CIDADE,
  CLIENTES.ESTADO AS UF,
  CLIENTES.FONE AS TELEFONE,
  CLIENTES.FAX AS FAX,
  CLIENTES.EMAIL AS EMAIL,
  case when CLIENTES.CNPJ = '' then CLIENTES.CPF else CLIENTES.CNPJ END as CNPJ_CNPF,
  case when CLIENTES.INSCRICAO = '' then CLIENTES.IDENTIDADE else CLIENTES.INSCRICAO END as IE_RG,
  CLIENTES.CONTATO AS CONTATO,
  CLIENTES.LIMITE AS LIMITE_CREDITO,
  CLIENTES.CEP AS CEP,
  CLIENTES.PAI AS PAI,
  CLIENTES.MAE AS MAE,
  CLIENTES.CADASTRO AS CADASTRO,
  CLIENTES.CELULAR AS CELULAR,
  CLIENTES.NUMERO AS NUMERO,
  CLIENTES.OBSERVACAO AS OBSERVACOES
FROM CLIENTES
