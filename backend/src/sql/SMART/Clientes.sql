SELECT
  CLIENTES.CODIGO AS CODIGO,
  CLIENTES.NOME AS NOME,
  CLIENTES.NOMEFANTASIA AS FANTASIA,
  CLIENTES.ENDERECO AS ENDERECO,
  CLIENTES.NUMERO AS NUMERO,
  CLIENTES.COMPLEMENTO AS COMPLEMENTO,
  CLIENTES.BAIRRO AS BAIRRO,
  CLIENTES.CEP AS CEP,
  CLIENTES.CIDADE AS CIDADE,
  CLIENTES.ESTADO AS UF,
  CLIENTES.CNPJ AS CNPJ_CNPF,
  case when CLIENTES.RG = '' then CLIENTES.INSCRICAO else CLIENTES.RG END as IE_RG,
  CLIENTES.TELEFONE AS TELEFONE,
  CLIENTES.CONTATO AS CONTATO,
  CLIENTES.FAX AS FAX,
  CLIENTES.OBSERVACOES AS OBSERVACOES,
  CLIENTES.NATURALIDADE AS NATURALIDADE,
  CLIENTES.NASCIMENTO AS NASCIMENTO,
  CLIENTES.ESTADOCIVIL AS EST_CIVIL,
  CLIENTES.CONJUGE AS CONJUGE,
  CLIENTES.PAI AS PAI,
  CLIENTES.MAE AS MAE,
  CLIENTES.PROFISSAO AS PROFISSAO,
  CLIENTES.EMAIL AS EMAIL,
  CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO,
  CLIENTES.DATA_CADASTRO AS CADASTRO,
  CLIENTES.INSCRICAO_MUNICIPAL AS IM,
  CLIENTES.CELULAR AS CELULAR
FROM CLIENTES
