SELECT
    CODIGO_CLIENTE AS CODIGO,
    DESCRICAO AS NOME,
    RAZAO_SOCIAL AS FANTASIA,
    CONTATO AS CONTATO,
    CPF_CNPJ AS CNPJ_CNPF,
    case when INSCRICAO_ESTADUAL = '' or INSCRICAO_ESTADUAL is null then RG else INSCRICAO_ESTADUAL END AS IE_RG,
    ENDERECO AS ENDERECO,
    NUMERO AS NUMERO,
    COMPLEMENTO AS COMPLEMENTO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    ESTADO AS UF,
    CEP AS CEP,
    TELEFONE1 AS TELEFONE,
    TELEFONE2 AS CELULAR,
    FAX AS FAX,
    -- CELULAR AS CELULAR,
    EMAIL AS EMAIL,
    PONTO_REFERENCIA  ||' /-/ '|| COMPLEMENTO AS OBSERVACOES,
    SEXO AS SEXO,
    PROFISSAO AS PROFISSAO,
    NOME_MAE AS MAE,
    NOME_PAI AS PAI,
    NATURALIDADE AS NATURALIDADE,
    LIMITE_CREDITO AS LIMITE_CREDITO,
    DATA_NASCIMENTO AS NASCIMENTO,
    DATA_ULTIMOMOVIMENTO AS ULTIMA_VANDA,
    DATA_CADASTRO AS CADASTRO
FROM CLIENTES
