SELECT
    C.COD_CLI AS CODIGO,
    C.NOME_CLI AS NOME,
    C.NOME_FANTASIA AS FANTASIA,
    C.CONTATO_CLI AS CONTATO,
    C.CNPJ_CLI AS CNPJ_CNPF,
    case when C.DOC_CLI = '' or C.DOC_CLI is null then C.INSC_ESTADUAL else C.DOC_CLI END AS IE_RG,
    C.ENDRES_CLI AS ENDERECO,
    C.PONTO_REFERENCIA AS COMPLEMENTO,
    C.NUMRES_CLI AS NUMERO,
    C.BAIRES_CLI AS BAIRRO,
    C.CIDRES_CLI AS CIDADE,
    C.ESTRES_CLI AS UF,
    C.CEPRES_CLI AS CEP,
    C.TELRES_CLI AS TELEFONE,
    C.CELULAR_CLI AS CELULAR,
    C.EMAIL_CLI AS EMAIL,
    C.PAI_CLI AS PAI,
    C.MAE_CLI AS MAE,
    C.CONJUGE_CLI AS CONJUGE,
    C.ESTCIVIL_CLI AS EST_CIVIL,
    C.LIMITE_CLI AS LIMITE_CREDITO,
    C.PROFISSAO_CLI AS PROFISSAO,
    case when C.ATIVO_CLI =  'S' then 'Ativo' else 'Inativo' end as SITUACAO,
    C.OBS_CLI AS OBSERVACPES,
    C.NASCIMENTO_CLI AS NASCIMENTO,
    C.DATACADASTRO_CLI AS CADASTRO
FROM CLIENTE AS C