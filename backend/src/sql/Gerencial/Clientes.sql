SELECT 
    CLIENTES.CODIGO AS CODIGO,
    CLIENTES.NOME AS NOME,
    CLIENTES.ENDERECO AS ENDERECO,
    CLIENTES.BAIRRO AS BAIRRO,
    CLIENTES.CIDADE AS CIDADE,
    CLIENTES.ESTADO AS UF,
    CLIENTES.CEP AS CEP,
    case when CLIENTES.CGC = '' or CLIENTES.CGC is null then CLIENTES.CPF else CLIENTES.CGC END as CNPJ_CNPF,
    CLIENTES.FONE AS TELEFONE,
    CLIENTES.FAX AS FAX,
    CLIENTES.ESTCIVIL AS EST_CIVIL,
    case when CLIENTES.IDENTIDADE = '' or CLIENTES.IDENTIDADE is null then CLIENTES.IDENTIDADE else CLIENTES.IDENTIDADE END as IE_RG,
    CLIENTES.DTNASC AS NASCIMENTO,
    CLIENTES.CONJUGE AS CONJUGE,
    CLIENTES.PAI AS PAI,
    CLIENTES.MAE AS MAE,
    CLIENTES.EMAIL_CLI AS EMAIL,
    CLIENTES.LIMITE AS LIMITE_CREDITO,
    CLIENTES.CONTATO AS CONTATO,
    CLIENTES.RAZAOSOCIAL AS FANTASIA,
    CLIENTES.DTCADASTRO AS CADASTRO
FROM CLIENTES
