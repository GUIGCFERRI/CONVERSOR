SELECT
	CODIGO,
	RAZAO AS NOME,
	FANTASIA,	
	ENDERECO,
	NUMERO,
	COMPLEMENTO,
	BAIRRO,
	CIDADE,
	UF,
	REPLACE (CEP,'.','') AS CEP,
	right(INSCRICAO, 20) AS IE_RG,	
	right(CGC,18) AS CNPJ_CNPF,
	INSCRICAOMUNICIPAL AS IM,	
	FONE AS TELEFONE,
	FAX,
	CELULAR,
	CONTATO,
	VENDEDOR AS PERSONAL1,
	LIMITE AS LIMITE_CREDITO,
	EMAIL,
	OBSERVACOES,
	DATE(NASCIMENTO) AS NASCIMENTO,
	DATE(DATACADASTRO) AS CADASTRO
FROM
	CLIENTES
