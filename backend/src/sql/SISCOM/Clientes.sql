SELECT
  CLIENTE.CODIGO AS CODIGO,
  CLIENTE.RAZAO_SOCIAL AS NOME,
  CLIENTE.NOME_FANTASIA AS FANTASIA,
  CLIENTE.ENDERECO AS ENDERECO,
  CLIENTE.NUMERO AS NUMERO,
  CLIENTE.COMPLEMENTO AS COMPLEMENTO,
  CLIENTE.BAIRRO AS BAIRRO,
  CIDADE.DESCRICAO AS CIDADE,
  CIDADE.UF AS UF,
  CLIENTE.FONE AS TELEFONE,
  CLIENTE.FAX AS FAX,
  CLIENTE.CELULAR AS CELULAR,
  CLIENTE.CONTATO AS CONTATO,
  case when CLIENTE.CNPJ = '' or CLIENTE.CNPJ is null then CLIENTE.CPF else CLIENTE.CNPJ END as CNPJ_CNPF,
  case when CLIENTE.INSCRICAO = '' or CLIENTE.INSCRICAO is null then CLIENTE.IDENTIDADE else CLIENTE.INSCRICAO END as IE_RG,
  CLIENTE.EMAIL AS EMAIL,
  CLIENTE.FILIACAO_PAI AS PAI,
  CLIENTE.FILIACAO_MAE AS MAE,
  CLIENTE.NATURALIDADE AS NATURALIDADE,
  CLIENTE.LIMITE_CREDITO AS LIMITE_CREDITO,
  CLIENTE.ESTADO_CIVIL AS EST_CIVIL,
  CLIENTE.SEXO AS SEXO,
  CLIENTE.CONJUGE_NOME AS CONJUGE,
  CLIENTE.OBSERVACOES AS OBSERVACOES,
  CLIENTE.CEP AS CEP
FROM CLIENTE
LEFT JOIN CIDADE ON CIDADE.ID_CIDADE = CLIENTE.ID_CIDADE
