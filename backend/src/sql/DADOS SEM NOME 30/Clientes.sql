SELECT
    CLIENTES.COD_CLIENTE AS CODIGO,
    CLIENTES.CLIENTE AS NOME,
    CLIENTES.RAZ_SOCIAL AS FANTASIA,
    case when CLIENTES.CPF = '' or CLIENTES.CPF is null then CLIENTES.CNPJ else CLIENTES.CPF END  AS CNPJ_CNPF,
    case when CLIENTES.RG = '' or CLIENTES.RG is null then CLIENTES.INC_EST else CLIENTES.RG END  AS IE_RG,
    CLIENTES.INSC_MUNICIPAL AS IM,
    CLIENTES.APELIDO AS CONTATO,
    CLIENTES.ENDER AS ENDERECO,
    CLIENTES.ENDER_NRO AS NUMERO,
    CLIENTES.PONTO_REF AS COMPLEMENTO,
    CLIENTES.BAIRRO AS BAIRRO,
    CIDADES.CIDADE AS CIDADE,
    CIDADES.UF AS UF,
    CLIENTES.CEP AS CEP,
    CLIENTES.FONE AS TELEFONE,
    CLIENTES.CEL AS CELULAR,
    CLIENTES.FAX AS FAX,
    CLIENTES.EMAIL AS EMAIL,
    CLIENTES.EST_CIVIL AS EST_CIVIL,
    CLIENTES.SEXO AS SEXO,
    CLIENTES.DT_NASC AS NASCIMENTO,
    CLIENTES.PAI AS PAI,
    CLIENTES.MAE AS MAE,
    CLIENTES.PROFISSAO AS PROFISSAO,
    CLIENTES.LTE_CREDITO AS LIMITE_CREDITO,
    CLIENTES.CONJUGE AS CONJUGE,
    CLIENTES.OBS AS OBSERVACOES,
    CLIENTES.DT_CADASTRO AS CADASTRO
FROM CLIENTES
LEFT JOIN CIDADES ON CIDADES.CODIGO = CLIENTES.COD_CIDADE
