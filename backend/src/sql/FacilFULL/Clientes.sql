SELECT
    CLI.CODIGO AS CODIGO,
    CLI.NOME AS NOME,
    CLI.FANTASIA AS FANTASIA,
    --CLI.CONTATO AS CONTATO,
    CLI_COMPLEMENTO_END AS COMPLEMENTO,
    CLI.CNPJ AS CNPJ_CNPF,
    CLI.INSCRICAO AS IE_RG,
    CLI.ENDERECO AS ENDERECO,
    CLI.NUMERO_ENDERECO AS NUMERO,
    CLI.BAIRRO AS BAIRRO,
    CLI.CIDADE AS CIDADE,
    CLI.UF AS UF,
    replace(CLI.CEP,'-','') AS CEP,
    replace(replace(replace(replace(CLI.FONE,'(',''),')',''),' ',' '),'-','')AS TELEFONE,
    replace(replace(replace(replace(CLI.FAX,'(',''),')',''),' ',' '),'-','')AS FAX,
    replace(replace(replace(replace(CLI.CELULAR,'(',''),')',''),' ',' '),'-','')AS CELULAR,
    CLI.EMAIL AS EMAIL,
    CLI.OBSERVACAO AS OBSERVACOES,
    CLI.LIMITECREDITO AS LIMITE_CREDITO,
    CLI.SALDODEVEDOR AS VALOR_A_RECEBER,
    --CLI.CLI_ESTADO_CIVIL AS EST_CIVIL,
    --CLI.CLI_PROFISAO AS PROFISSAO,
    CLI.DTNASCIMENTO AS NASCIMENTO,
    CLI.DTULTIMACOMPRA AS ULTIMA_COMPRA,
    CLI.DTCADASTRO AS CADASTRO,
    case when CLI.CLI_ATIVO = 'True' then 'Ativo' else 'Inativo' end as SITUACAO
FROM SMCLIENTES CLI
WHERE CLI.codigo <> '999999'
