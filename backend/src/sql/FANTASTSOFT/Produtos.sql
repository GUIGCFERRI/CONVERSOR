SELECT 
PRODUTOS.CODIGO,
PRODUTOS.CODIGOSWEDA AS COD_FABRICANTE,
PRODUTOS.DESCRICAO,
PRODUTOS.UNIDADE AS UND,
PRODUTOS.PRECOCUSTO AS PRECO_CUSTO,
PRODUTOS.CUSTO_MEDIO, 
PRODUTOS.MARGEMBRUTA AS MARGEM_LUCRO,
PRODUTOS.PRECO AS PRECO_VENDA,
--PRODUTOS.SITUACAOTRIB AS ST,
--PRODUTOS.SITUACAOTRIB_NFE AS ST,
PRODUTOS.NFE_CSOSN AS ST,
PRODUTOS.COMISSAO,
PRODUTOS.ESTOQUEMINIMO AS QTD_IDEAL, 
PRODUTOS.ESTOQUE AS QTD,
PRODUTOS.ALIQUOTAIPI AS ALIQ_IPI,
PRODUTOS.ALIQUOTAIPIVENDA AS ALIQ_IPI_VENDA,
PRODUTOS.ALIQPIS_ENTRADA AS PISE_ALIQ_NOR,
PRODUTOS.ALIQPIS_SAIDA AS PIS_ALIQ_NOR,
PRODUTOS.ALIQCOFINS_ENTRADA AS COFINSE_ALIQ_NOR,
PRODUTOS.ALIQCOFINS_SAIDA AS COFINS_ALIQ_NOR,
PRODUTOS.IPI_CST_ENTRADA AS IPI_CODIGO,
PRODUTOS.IPI_CST_SAIDA AS IPI_CODIGO_VENDA,
PRODUTOS.PIS_CST_ENTRADA AS PISE_CODIGO,
PRODUTOS.PIS_CST_SAIDA AS PIS_CODIGO,
PRODUTOS.COFINS_CST_ENTRADA AS COFINSE_CODIGO,
PRODUTOS.COFINS_CST_SAIDA AS COFINS_CODIGO,
PRODUTOS.NCM AS COD_NCM,
PRODUTOS.OBSERVACAO AS OBSERVACOES,
PRODUTOS.DATAULTCOMPRA AS ULTIMA_COMPRA,
PRODUTOS.CEST AS PERSONAL6,
PRODUTOS.IPPT,
PRODUTOS.ORIGEM_SN AS OST,
PRODUTOS.PESO_PRODUTO AS PESO,
PRODUTOS.CODIGO_ANP AS COD_ANP,
PRODUTOS.CFOP_DENTRO AS CF,
LEFT (REPLACE(PRODUTOS.CFOP_DENTRO,'.',''),3) AS ELO,
CAST(PRODUTOS.DATACRIADO AS DATE) AS DATA_CADASTRO,
CASE PRODUTOS.DESATIVADO WHEN '0' THEN 'Ativo' ELSE 'Inativo' END AS SITUACAO, 
PRODUTOSFAMILIA.DESCRICAO AS FAMILIA
FROM PRODUTOS
LEFT JOIN PRODUTOSFAMILIA ON PRODUTOSFAMILIA.CODIGO = PRODUTOS.FAMILIA 
