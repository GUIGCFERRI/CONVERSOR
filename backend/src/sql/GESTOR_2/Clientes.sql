SELECT
    PESSOA AS CODIGO,
    NOME AS NOME,
    FANTASIA AS FANTASIA,
    CICCGC AS CNPJ_CNPF,
    INS_MUN AS IM,
    NRDOC AS IE_RG,
    ENDERECO AS ENDERECO,
    ENDNUM AS NUMERO,
    COMPLEMENTO AS COMPLEMENTO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    UF AS UF,
    CEP AS CEP,
    DDD ||''|| FONE AS TELEFONE,
    DDD ||''|| FAX AS FAX,
    EMAIL AS EMAIL,
    SEXO AS SEXO,
    MAE AS MAE,
    ESTADOCIVI AS EST_CIVIL,
    NOMCJ AS CONJUGE,
    LIMITECRED AS LIMITE_CREDITO,
    CAST (DATNAS AS DATE) AS NASCIMENTO,
    CAST (DATA_CADASTRO AS DATE) AS CADASTRO
FROM CLIENTES
WHERE NOME <> ''
