SELECT
    CLICOD AS CODIGO,
    CLINOME AS NOME,
    CLIFANTASIA AS FANTASIA,
    CLICONTATO AS CONTATO,
    CLINASCIMENTO AS NASCIMENTO,
    CAST (CLICADASTRO AS DATE) AS CADASTRO,
    CLISEXO AS SEXO,
    case when REPLACE(replace(CLICPF,'.',''),'-','') = '' or REPLACE(replace(CLICPF,'.',''),'-','') is null then REPLACE(replace(replace(CLICNPJ,'-',''),'.',''),'/','') else REPLACE(replace(CLICPF,'.',''),'-','') END AS CNPJ_CNPF,
    case when CLIIDENTIDADE = '' or CLIIDENTIDADE is null then CLIINSCESTADUAL else CLIIDENTIDADE END AS IE_RG,
    CLIENDERECO AS ENDERECO,
    CLINUMERO AS NUMERO,
    CLICOMPLEMENTO AS COMPLEMENTO,
    CLIBAIRRO AS BAIRRO,
    CLICIDADE AS CIDADE,
    CLIUF AS UF,
    CLICEP AS CEP,
    CLIEMAIL AS EMAIL,
    CLITELEFONE AS TELEFONE,
    CLICELULAR AS CELULAR,
    CLIFAX AS FAX,
    CLIOBS AS OBSERVACOES,
    CAST (CLIULTCOMPRA AS DATE) AS ULTIMA_COMPRA,
    CLIESTADOCIVIL AS EST_CIVIL
FROM CLIENTE
