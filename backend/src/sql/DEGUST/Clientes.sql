SELECT
  CADCLIENTE.CLI_CODIGO AS CODIGO,
  CADCLIENTE.CLI_NOME AS NOME,
  CADCLIENTE.CLI_ENDERECO AS ENDERECO,
  CADCLIENTE.CLI_COMPL AS COMPLEMENTO,
  CADCLIENTE.CLI_BAIRRO AS BAIRRO,
  CADCIDADE.CID_NOME AS CIDADE,
  CADCLIENTE.CLI_CEP AS CEP,
  CADCLIENTE.CLI_EMAIL AS EMAIL,
  CADCLIENTE.CLI_FONE AS TELEFONE,
  CADCLIENTE.CLI_DTNASC AS NASCIMENTO,
  CADCLIENTE.CLI_DTCAD AS CADASTRO,
  CADCLIENTE.CLI_OBSERVACAO AS OBSERVACOES,
  CADCLIENTE.CLI_CIDADE AS CIDADE,
  CADCLIENTE.CLI_UF AS UF,
  CADCLIENTE.CLI_LIMITEDBT AS LIMITE_CREDITO,
  CADCLIENTE.CLI_ENDNUM AS NUMERO,
  CADCLIENTE.CLI_CPFCNPJ AS CNPJ_CNPF,
  CADCLIENTE.CLI_IE AS IE_RG,
  CADCLIENTE.CLI_CELULAR AS CELULAR,
  CADCLIENTE.CLI_IM AS IM,
  CADCLIENTE.CLI_FONE2 AS FAX,
  CADCLIENTE.CLI_SEXO AS SEXO
FROM CADCLIENTE
LEFT JOIN CADCIDADE ON CADCIDADE.CID_CODIGO = CADCLIENTE.CID_CODIGO
