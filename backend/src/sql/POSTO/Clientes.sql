SELECT
    CODIGO AS CODIGO,
    RAZAOSOCIAL AS NOME,
    NOMEFANTASIA AS FANTASIA,
    CONTATO AS CONTATO,
    CGC_CPF AS CNPJ_CNPF,
    case when RG = '' or RG is null then INSCRICAO else RG END AS IE_RG,
    ENDE AS ENDERECO, 
    NUMERO AS NUMERO,
    BAIRRO AS BAIRRO,
    CIDADE AS CIDADE,
    UF AS UF,
    CEP AS CEP,
    CELULAR AS CELULAR,
    FAX AS FAX,
    FONE AS TELEFONE,
    ENDE_EMAIL AS EMAIL,
    LIMITE AS LIMITE_CREDITO,
    HISTORICO AS OBSERVACOES,
    CAST (DATACAD AS DATE) AS CADASTRO
FROM CLIENTES 