select UN05000.CODIGO,
UN05911.DESCRICAO AS FAMILIA,
UN05910.DESCRICAO AS GRUPO,
UN05000.DESCRICAO,
UN05000.UNVENDA AS UND,
UN05000.NCM AS COD_NCM,
UN05000.CODFAB AS COD_FABRICANTE,
UN05000.CODEAN AS BARRAS,
UN05000.CEST AS PERSONAL6,
UN05100.ESTOQUE AS QTD,
UN05100.CUSTO AS PRECO_CUSTO,
UN05100.PRECOVENDA AS PRECO_VENDA
FROM UN05000
LEFT JOIN UN05911 ON UN05000.SUBGRUPO=UN05911.CODIGO
LEFT JOIN UN05910 ON UN05000.GRUPO=UN05910.CODIGO
LEFT JOIN UN05100 ON UN05000.CODIGO=UN05100.CODPRODUTO

