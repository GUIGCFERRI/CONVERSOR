SELECT DM001.ID_DM001 AS CODIGO,
DM001.NM_RAZAO_SOCIAL AS NOME,
DM001.NM_FANTASIA AS FANTASIA,
DM001.NR_CNPJ AS CNPJ_CNPF,
DM001.NR_INSC_EST AS IE_RG,
DM001.NM_LOGRADOURO AS ENDERECO,
DM001.NR_LOGRADOURO AS NUMERO,
DM001.DS_COMPLEMENTO AS COMPLEMENTO,
DM025.NM_CIDADE AS CIDADE,
DM024.DS_UF AS UF,
DM001.NM_BAIRRO AS BAIRRO,
DM001.NR_CEP AS CEP,
DM001.NM_CONTATO AS CONTATO,
DM001.DS_EMAIL AS EMAIL,
DM001.NR_DDD||DM001.NR_FONE AS TELEFONE,
DM001.NR_DDD_CELULAR||DM001.NR_FONE_CELULAR AS CELULAR
FROM DM001
LEFT JOIN DM025 ON DM001.ID_DM025=DM025.ID_DM025
LEFT JOIN DM024 ON DM025.ID_DM024=DM024.ID_DM024
WHERE DM001.IN_PESSOA='C'





