SELECT
  CLIENTES.CODIGO_CLIENTE AS CODIGO,
  CLIENTES.DESCRICAO AS NOME,
  CLIENTES.RAZAO_SOCIAL AS FANTASIA,
  CLIENTES.ENDERECO AS ENDERECO,
  CLIENTES.NUMERO AS NUMERO,
  CLIENTES.BAIRRO AS BAIRRO,
  CLIENTES.CIDADE AS CIDADE,
  CLIENTES.ESTADO AS UF,
  CLIENTES.CEP AS CEP,
  CLIENTES.TELEFONE1 AS TELEFONE,
  CLIENTES.FAX AS FAX,
  CLIENTES.CELULAR AS CELULAR,
  CLIENTES.DATA_NASCIMENTO AS NASCIMENTO,
  CLIENTES.DATA_CADASTRO AS CADASTRO,
  CLIENTES.CPF_CNPJ AS CNPJ_CNPF,
  case when CLIENTES.INSCRICAO_ESTADUAL = '' or CLIENTES.INSCRICAO_ESTADUAL is null or CLIENTES.INSCRICAO_ESTADUAL = 'NC' then CLIENTES.RG else CLIENTES.INSCRICAO_ESTADUAL END as IE_RG,
  CLIENTES.INSCRICAO_ESTADUAL,
  CLIENTES.RG,
  CLIENTES.COMPLEMENTO AS COMPLEMENTO,
  CLIENTES.CONTATO AS CONTATO,
  CLIENTES.SEXO AS SEXO,
  CLIENTES.PROFISSAO AS PROFISSAO,
  CLIENTES.NOME_MAE AS MAE,
  CLIENTES.NOME_PAI AS PAI,
  CLIENTES.NATURALIDADE AS NATURALIDADE,
  CLIENTES.EMAIL AS EMAIL,
  CLIENTES.OBSERVACAO AS OBSERVACOES,
  CLIENTES.LIMITE_CREDITO AS LIMITE_CREDITO
FROM CLIENTES
