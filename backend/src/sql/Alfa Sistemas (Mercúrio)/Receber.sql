SELECT
    C000049.CODIGO AS DOCUMENTO,
    C000049.CODCLIENTE AS COD_CLIENTE,
    C000007.NOME AS NOM_CLIENTE,
    C000049.DATA_EMISSAO AS EMISSAO,
    C000049.DATA_VENCIMENTO AS VENCIMENTO,
    C000049.DATA_PAGAMENTO AS RECEBIMENTO,
    C000049.VALOR_ORIGINAL AS VALOR_DUP,
    C000049.VALOR_PAGO AS VALOR_REC,
    C000049.VALOR_JUROS AS VALOR_JUR,
    C000049.VALOR_DESCONTO AS VALOR_DES,
    C000049.DOCUMENTO AS HISTORICO,
    C000049.HISTORICO AS OBSERVACOES,
    C000049.NRO_PARCELA AS NUM_PARCELA,
    C000007.cpf AS CNPJ
FROM C000049
LEFT JOIN C000007 ON C000049.CODCLIENTE = C000007.CODIGO
