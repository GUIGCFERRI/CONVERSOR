SELECT
  FORN0000.CODIGO AS CODIGO,
  FORN0000.DESCRICAO AS NOME,
  FORN0000.FANTASIA AS FANTASIA,
  FORN0000.ENDERECO AS ENDERECO,
  FORN0000.BAIRRO AS BAIRRO,
  FORN0000.CIDADE AS CIDADE,
  FORN0000.ESTADO AS UF,
  FORN0000.CEP AS CEP,
  case when FORN0000.CNPJ = '' or FORN0000.CNPJ is null then FORN0000.CPF else FORN0000.CNPJ END as CNPJ_CNPF,
  FORN0000.CONTATO AS CONTATO,
  FORN0000.OBSERVACAO AS OBSERVACOES,
  FORN0000.INSCESTADUAL AS IE_RG,
  FORN0000.TELEFONEI AS TELEFONE,
  FORN0000.FAX AS FAX,
  FORN0000.CELULAR AS CELULAR,
  FORN0000.EMAIL AS EMAIL,
  FORN0000.NUMERO AS NUMERO
FROM FORN0000
