SELECT
  CLIE0000.CODIGO AS CODIGO,
  CLIE0000.DESCRICAO AS NOME,
  CLIE0000.FANTASIA AS FANTASIA,
  CLIE0000.ENDERECO AS ENDERECO,
  CLIE0000.NUMERO AS NUMERO,
  CLIE0000.BAIRRO AS BAIRRO,
  CLIE0000.CIDADE AS CIDADE,
  CLIE0000.ESTADO AS UF,
  CLIE0000.CEP AS CEP,
  case when CLIE0000.CNPJ = '' or CLIE0000.CNPJ is null then CLIE0000.CPF else CLIE0000.CNPJ END as CNPJ_CNPF,
  CLIE0000.CONTATO AS CONTATO,
  CAST (CLIE0000.NASCIMENTO AS DATE) AS NASCIMENTO,
  CLIE0000.OBSERVACAO AS OBSERVACOES,
  CLIE0000.LIMITE AS LIMITE_CREDITO,
  CAST (CLIE0000.CLIENTEDESDE AS DATE) AS CADASTRO,
  CLIE0000.PAI AS PAI,
  CLIE0000.MAE AS MAE,
  CLIE0000.CONJUGE AS CONJUGE,
  case when CLIE0000.IDENTIDADE = '' or CLIE0000.IDENTIDADE is null then CLIE0000.INSCRICAO else CLIE0000.IDENTIDADE END as IE_RG,
  CLIE0000.ESTADOCIVIL AS EST_CIVIL,
  CLIE0000.INSCRICAOMUNICIPAL AS IM,
  CLIE0000.TELEFONEI AS TELEFONE,
  CLIE0000.FAX AS FAX,
  CLIE0000.CELULAR AS CELULAR,
  CLIE0000.EMAIL AS EMAIL,
  CLIE0000.SEXO AS SEXO
FROM CLIE0000
