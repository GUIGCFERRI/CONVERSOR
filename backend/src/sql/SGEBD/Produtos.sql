SELECT
  TABEST1.CODIGO AS COD_FABRICANTE,
  --SUBSTRING (TABEST1.CONTROLE FROM 2 FOR 6) AS CODIGO,
  TABEST1.CONTROLE AS CODIGO,
  TABEST1.CODIGOAUXLIAR AS CARACTERISTICAS,
  TABEST1.PRODUTO AS DESCRICAO,
  TABFORN.EMPRESA AS FORNECEDOR,
  TABEST1.PRECOCUSTO AS PRECO_CUSTO,
  TABEST1.CUSTOMEDIO AS CUSTO_MEDIO,
  TABEST1.PRECOVENDA AS PRECO_VENDA,
  TABEST1.UNIDADE AS UND,
  TABEST1.ULTREAJ AS ALTERACAO_PRECO,
  TABEST1.OBS AS OBSERVACOES,
  TABEST1.NCM AS COD_NCM,
  SUBSTRING (TABEST1.CST FROM 2 FOR 3) AS ST,
  --SUBSTRING (TABEST1.CSOSN FROM 2 FOR 4) AS ST,
  TABEST1.UNID_COMPRA AS UND_COMPRA,
  TABEST1.DATACADASTRO AS DATA_CADASTRO,
  TABEST1.CEST AS PERSONAL6,
  TABEST1.ESTMINIMO AS QTD_INICIAL,
  TABEST1.QUANT AS QTD,
  TABEST1.MARGEMREVENDA AS MARGEM_LUCRO,
  TABEST1.PESO AS PESO,
  TABEST1.GTINEMB AS BARRAS
FROM TABEST1
LEFT JOIN TABFORN ON TABFORN.CODIGO = TABEST1.LKFORNEC
