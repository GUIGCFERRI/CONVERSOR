SELECT
    COD_CLI AS CODIGO,
    RAZAO_CLI AS NOME,
    FANT_CLI AS FANTASIA,
    case when CPF_CLI = '' or CPF_CLI is null then CNPJ_CLI else CPF_CLI END AS CNPJ_CNPF,
    case when RG_CLI = '' or RG_CLI is null then IE_CLI else RG_CLI END AS IE_RG,
    END_CLI AS ENDERECO,
    NUMEND_CLI AS NUMERO,
    COMPLE_CLI AS COMPLEMENTO,
    BAIRRO_CLI AS BAIRRO,
    CEP_CLI AS CEP,
    CID_CLI AS CIDADE,
    UF_CLI AS UF,
    EMAIL_CLI AS EMAIL,
    FONE_CLI AS TELEFONE,
    CEL_CLI AS CELULAR,
    NOMEPAI_CLI AS PAI,
    NOMEMAE_CLI AS MAE,
    LIMITE_CLI AS LIMITE_CREDITO,
    DATANASC_CLI AS NASCIMENTO,
    OBS_CLI AS OBSERVACOES,
    CAST (DATACAD_CLI AS DATE) AS CADASTRO,
    SEXO_CLI AS SEXO,
    ESTCIVIL AS EST_CIVIL
FROM CLIENTES
WHERE RAZAO_CLI <> '';